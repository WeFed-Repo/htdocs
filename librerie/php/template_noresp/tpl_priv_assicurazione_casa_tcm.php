		<h1>TCM - Temporanea Caso Morte</h1>
		<div class="abstract">
			<p>Tutela il tuo mutuo, proteggi la tua famiglia</p>
		</div>
		<div class="text-section">
			<p>La<strong> polizza a copertura del mutuo TCM</strong> tutela coloro che hanno sottoscritto un mutuo e le loro famiglie perch&eacute;, in caso di decesso o invalidit&agrave; grave dell'Assicurato, evita che i suoi familiari debbano accollarsi il debito residuo.&nbsp;Con validit&agrave; pari all&rsquo;intera durata del finanziamento contratto, <strong>TCM </strong>ti consente di designare i <strong>beneficiari </strong>che avranno diritto al capitale assicurato.<br /> <br /> In pi&ugrave;, &egrave; facilmente accessibile: ti basta sottoscrivere una <strong>dichiarazione </strong>di buono stato di salute.</p>
		</div>  
	</div>
	<div class="content">
		<div class="text-section">
			<p>Per il primo anno, il premio viene calcolato in base al numero di giorni intercorrenti tra la data di erogazione del mutuo e il 31 dicembre dello stesso anno, mentre i premi successivi sono calcolati annualmente ed equivalgono al capitale assicurato moltiplicato per 0,16% (<strong>tasso di premio</strong>).</p>
		</div>
		
