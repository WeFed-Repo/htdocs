<h1>Webank e la trasparenza</h1> 
<p>
	La fiducia nei rapporti con i nostri clienti è un valore che ci appartiene e che vogliamo costruire attraverso comportamenti trasparenti. <br>
La trasparenza si realizza quando le informazioni e le condizioni sono accessibili, ma anche e soprattutto comprensibili. Ecco perché fondiamo la relazione con i nostri clienti sui principi di <strong>chiarezza</strong>, <strong>comprensibilità</strong> e <strong>completezza</strong> della comunicazione.<br><br>

In queste pagine trovi tutto quello che c'è da sapere sui nostri <strong>contratti</strong>, su <strong>quanto costa Conto Webank</strong> e sulle novità che riguardano l'efficienza e la trasparenza dei servizi di pagamento secondo la <strong>nuova normativa europea</strong>. <br><br>

Per leggere le condizioni economiche che regolano conti correnti, carte di credito, mutui, prestiti, servizi di trading eccetera vai alla sezione <strong>Fogli informativi</strong>.<br><br>

Qui sotto trovi invece alcune <strong>guide</strong> che ti aiutano a conoscere meglio i servizi bancari e scoprire come funzionano.
