		<h1>BPM Infortuni</h1>
		<div class="abstract">
			<p>La polizza tutto-in-uno. Facile, personalizzabile e vantaggiosa.</p>
		</div>
		<div class="text-section">
			<p>Con Banca Popolare di Milano fronteggiare gli imprevisti &egrave; pi&ugrave; facile che mai. <br>
			<strong>Polizza Infortuni</strong> &egrave; la soluzione <strong>chiara, semplice e personalizzabile</strong>. Per la tua protezione e quella dei tuoi cari, nel lavoro come nel tempo libero. <br>Sempre.<br>
Tutela dagli infortuni, da quello pi&ugrave; &quot;piccolo&quot; a quello pi&ugrave; &quot;grave&quot;. <br>
Offre coperture con franchigie che puoi scegliere e modulare secondo le tue esigenze.			
<br /> <br /> <strong>Polizza infortuni</strong> &egrave; dedicata ai lavoratori e ai liberi professionisti, ma anche ai pi&ugrave; giovani e ai non lavoratori.</p>
