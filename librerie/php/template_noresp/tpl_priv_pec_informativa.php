<h2>COS'È LA POSTA ELETTRONICA CERTIFICATA (PEC)</h2>
<div class="rowwrapper">
		 <div class="boxstepCont green">	
			<div class="boxstep green">
				
				<div class="top green"><span class="angololeft"></span><span class="angoloright"></span></div>
				<div class="middle">
				<div class="text">
					
					<p>La Posta Elettronica Certificata (PEC) &egrave; il  sistema che consente di <strong>inviare email</strong> con valore legale equiparato a una raccomandata 
postale con ricevuta di ritorno.</p>
<p>In altre parole, chi invia una email riceve, sempre via email, <strong>l'attestazione che il messaggio &egrave; stato inviato e consegnato al 
destinatario.</strong>
</p><p>Per questo motivo, si parla di posta &quot;<strong>certificata</strong>&quot;, perché viene rilasciata a tutti gli effetti una &quot;ricevuta&quot; con data e orario dell'invio e 
della ricezione.</p><p>I vantaggi sono evidenti: <strong>risparmio di carta</strong>, di <strong>spese postali</strong>, <strong>certezza della consegna del messaggio</strong>. E, soprattutto, <strong>velocit&agrave;
di consegna</strong>: i tempi sono praticamente azzerati rispetto alla posta tradizionale.</p>
<p>Inoltre, il sistema di posta certificata, grazie ai protocolli di sicurezza utilizzati dal gestore (InfoCert), &egrave; in grado di garantire la <strong>certezza 
del contenuto</strong>, impedendo modifiche al messaggio, sia per quanto riguarda i contenuti, sia eventuali allegati.</p>
