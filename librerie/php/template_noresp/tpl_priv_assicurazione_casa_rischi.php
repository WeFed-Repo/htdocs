		<h1>Rischi casa</h1>
		<div class="abstract">
			<p>Stai sottoscrivendo un mutuo? Proteggi la tua casa</p>
		</div>
		<div class="text-section">
			<p>La polizza <strong>Rischi Casa</strong>, ad adesione facoltativa, protegge gli immobili dati a garanzia in caso di mutui ipotecari concessi a privati da <strong>Banca Popolare di Milano</strong>.<br /> L&rsquo;addebito del premio viene posticipato e frazionato a seconda della rateizzazione del mutuo.</p>
			<p><strong>VANTAGGI DELLA POLIZZA RISCHI CASA</strong></p>	
			<ul class="biglist"> 
				<li><i class="icon-ok-sign"></i><span>garanzie complete a copertura dell&rsquo;immobile assicurato</span></li>
				<li><i class="icon-ok-sign"></i><span>rateizzazione mensile del premio</span></li>
				<li><i class="icon-ok-sign"></i><span>addebito diretto del premio sul conto corrente.</span></li>
			</ul>
		</div>  
	</div>
	<div class="content">
		<div class="text-section">
