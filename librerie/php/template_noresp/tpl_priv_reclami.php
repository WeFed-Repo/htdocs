<h1>Reclami</h1>
<p>In quest'area puoi trovare tutto quello che c'&egrave; da sapere sul nostro <strong>Servizio Reclami</strong>, richiedere chiarimenti e comunicarci particolari criticit&agrave;. </p>
<h3 class="verde">Quando inviare un reclamo</h3>
<p>Potrai inviarci un reclamo, con una richiesta formale, in caso di <strong>contestazione</strong> verso la Banca su <strong>comportamenti</strong> oppure <strong>problematiche</strong> inerenti ai prodotti e ai servizi bancari, finanziari e d'investimento. 
Webank risponder&agrave; alla tua richiesta nel <strong>minor tempo possibile</strong> e nel rispetto della <a href="#">normativa in vigore</a>. I tempi massimi di risposta e di risoluzione, infatti, variano in base al tipo di reclamo: 
</p>
<ul class="lista">
   <li class="lista">servizi bancari e finanziari (ad es. conto corrente, carte di credito, mutui e finanziamenti…): entro 30 giorni</li>
   <li class="lista">servizi di investimento (trading): da 30 fino a un massimo di 90 giorni</li>
   <li class="lista">privacy: entro 15 giorni</li>
</ul>
<h3 class="verde">Il Servizio Reclami &egrave; ancora pi&ugrave; semplice e intuitivo</h3>
<p>Per offrirti un servizio sempre pi&ugrave; adatto alle tue esigenze, abbiamo <strong>semplificato</strong> la procedura d'invio dei tuoi reclami, rendendola molto pi&ugrave; <strong>intuitiva</strong>. Per una risoluzione pi&ugrave; veloce ed efficace, infatti, ti baster&agrave; <strong>compilare</strong> il <strong>form</strong> che trovi in questa pagina, selezionare l'<strong>area di riferimento</strong> del tuo reclamo (banking, trading, privacy), inserire il tuo indirizzo email con il testo del reclamo e inviare. </p>
<p>Nel caso di semplici segnalazioni o suggerimenti relativi ai prodotti e ai servizi offerti da Webank puoi, invece, contattare in qualunque momento il nostro Servizio Clienti al numero 800 060 070 oppure inviare un'email a <a href="mailto:webank@webank.it">webank@webank.it</a>.</p>
