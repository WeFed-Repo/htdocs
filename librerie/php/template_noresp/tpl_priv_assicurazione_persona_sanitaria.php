			<p>Di seguito le polizze a tua disposizione:</p> 
			<ul class="biglist">     
				<li><i class="icon-ok-sign"></i><span><strong>Bpm Doc</strong></span></li>     
				<li><i class="icon-ok-sign"></i><span><strong>Bpm Doc Diaria</strong></span></li>
			</ul>
		</div>
		<div class="separator"></div>
		<div class="text-section">
			<p><strong>BPM DOC <br /> </strong><br /> Con <strong>BPM Doc</strong> hai a disposizione 250.000 euro per garantirti un futuro sereno, con la possibilit&agrave; di decidere liberamente in quale luogo del mondo preferisci essere curato. <br /> <br /> <strong>BPM Doc</strong> ha un <strong>costo straordinariamente contenuto</strong>, a fronte di<strong> prestazioni complete</strong>: potrai decidere se affidarti alla struttura pubblica e ricevere un consistente indennizzo, oppure a cliniche private in Italia e all'estero, ed essere rimborsato delle spese sostenute.</p> 
			<p>E se preferisci, puoi far pagare le prestazioni <strong>direttamente a Bipiemme Vita </strong>presso un'estesa rete di strutture private convenzionate.</p> 
			<p>In pi&ugrave;, <strong>BPM Doc </strong>ti fa scegliere tra due opzioni:</p> 
			<ul class="biglist">     
				<li><i class="icon-ok-sign"></i><span><strong>BPM Doc mirato</strong>, per la copertura delle spese sostenute durante un ricovero per un <strong>grande intervento chirurgico</strong> (per esempio un intervento per bypass o un trapianto) causato da una malattia o da un infortunio</span></li>     
				<li><i class="icon-ok-sign"></i><span><strong>BPM Doc extra</strong>, la soluzione pi&ugrave; completa che interviene a copertura delle spese sostenute per <strong>qualunque ricovero</strong>; inoltre, ti rimborsa anche per <strong>prestazioni indipendenti dall'esistenza di un ricovero</strong> come quelle relative agli esami di alta diagnostica (Tac, Risonanza magnetica, elettrocardiografia). Si tratta di una copertura ideale per la famiglia che può comodamente sostenere il premio con piccole rate mensili e, per determinati nuclei, godere di significative agevolazioni.</span></li>
			</ul>
			<p><strong><br> BPM DOC DIARIA</strong></p>
			<p><strong>BPM Doc Diaria</strong> &egrave; la polizza sanitaria innovativa che interviene <strong>subito</strong>, in modo efficace, se devi affrontare un intervento chirurgico, in ambulatorio o in ospedale.&nbsp;Con <strong>BPM Doc Diaria</strong> puoi scegliere liberamente in che struttura curarti, che sia pubblica o privata, in Italia o all’estero, e ottenere per ogni giorno di degenza una diaria giornaliera di importo variabile in relazione all'importanza e alla difficolt&agrave; dell'operazione.<br> &nbsp;</p>
