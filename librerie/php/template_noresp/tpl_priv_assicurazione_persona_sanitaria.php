			<ul class="checklist">
				<li><i class="icon-ok"></i><span>vuoi una polizza sanitaria personalizzata completa ma semplice</span></li>
				<li><i class="icon-ok"></i><span>vuoi avere la possibilit&agrave; di farti rimborsare anche le prestazioni come l&rsquo;alta diagnostica<br /></span></li>
