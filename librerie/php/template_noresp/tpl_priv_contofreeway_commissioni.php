<p>Per contratto applichiamo <b>il profilo standard</b>: paghi una commissione su ogni operazione. Se preferisci, puoi anche pagare un importo fisso una sola volta all'anno e fare tutte le operazioni che vuoi attivando il <b> profilo Banking Forfait</b>.<br><br><b>PASSA A BANKING FORFAIT</b><br>Banking Forfait ti permette di eliminare le commissioni sulle singole operazioni. Pagando solo <b>18 euro</b>, per un anno solare (da gennaio a dicembre) puoi fare bonifici - ordinari e periodici - e pagare bollettini - postali e Freccia - <b>senza commissioni</b>, spese postali escluse.</p>
