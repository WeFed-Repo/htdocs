			img.attr('src', '/img/ret/ico_quest_spunta.gif');
			$('#questTest .expandable.questDom').each(function () {
				var inputArr = $(this).find('input[type=radio]'),
					checkedOK = false;
				inputArr.each(function () {
					var input = $(this);
					if (input.prop('checked')) {
						checkedOK = true;
						if (input.val() == 'si') {
							disabled = true;
						}
					}
				});
				if (!checkedOK) {
					disabled = true;
				}
				return !disabled;
			});
			checkBox.prop('disabled', disabled);
		}
	});
	checkBox.click(function () {
		if (this.checked) {
			radioArr.prop('disabled', true);
			activeBtn(questNext, function () {
				checkBox.prop('disabled', false);
				activeNextAccBox(questNext);
			});
		} else {
			radioArr.prop('disabled', false);
			disactiveBtn(questNext);
		}
	});
});
