$(function () {
	var radioArr = $('#questTest input[type=radio]'),
		checkBox = $('#checkInfo'),
		questNext = $('#questNext');
	radioArr.click(function () {
		var el = $(this),
			questDom = el.closest('.questDom'),
			img = questDom.find('img.bot'),
			disabled = false;
		if (el.val() == 'si') {
			img.attr('src', '/img/ret/ico_quest_x.gif');
			checkBox.prop('disabled', true);
			openPopOverLayer('layeralert2', 'floating', 2);
