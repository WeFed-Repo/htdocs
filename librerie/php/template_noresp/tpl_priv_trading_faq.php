				<h2>Posizione fiscale</h2>
				<p>
					Il <strong>capital gain</strong> relativo alle operazioni di trading verr&agrave; calcolato di giorno in giorno e addebitato nel caso di plusvalenze alla fine del mese successivo a quello in cui sono maturate.<br>
					<br>
					Le minusvalenze accumulate verranno <strong>trasferite sul nuovo rapporto Webank</strong> e possano essere compensate nell'anno in cui si realizzano e nei 4 anni successivi.<br>
					<br>
					Questo meccanismo consente, per esempio, di poter recuperare le perdite realizzate nel 2007 fino al 31/12/2011, superata tale data si perde la possibilit&agrave; di utilizzare questo credito d'imposta.
