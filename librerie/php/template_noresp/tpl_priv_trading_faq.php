				<h2>Inserire un ordine</h2>
				<p>
					Per inserire un ordine &egrave; necessario seguire questo percorso: <strong>Trading > Compravendita > Inserimento ordini</strong>.<br>
					<br>
					Per cercare il titolo, utilizza la <strong>descrizione</strong>, il <strong>simbolo</strong> o il <strong>codice Isin</strong>.<br>
					<br>
					Una volta individuato il titolo, indica <strong>quantit&agrave;</strong>, <strong>prezzo</strong> dell'ordine e clicca sul bottone Compra o Vendi.<br>
					<br>
					Selezionando la voce  "<strong>marginata intraday</strong>" o "<strong>marginata overnight</strong>" sfrutti l'acquisto o la vendita di un titolo in modalit&agrave; marginazione. <br>
					<br>
					Se vuoi impostare un ordine in short selling overnight seleziona la tipologia di ordine corrispondente.<br>
