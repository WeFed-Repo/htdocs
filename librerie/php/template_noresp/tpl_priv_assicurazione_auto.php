		<h1>Multiprotezione Auto</h1>
		<div class="abstract">
			<p>La polizza auto che si adatta al tuo stile di guida</p>
		</div>
		<div class="text-section">
			<p><strong>Multiprotezione Auto</strong> &egrave; la polizza che ti d&agrave; una copertura completa e modulabile per la tua auto: oltre alla garanzia <strong>RCA </strong>obbligatoria, ti fa scegliere tra quattro formule predefinite,&nbsp;adattandosi cos&igrave; al tuo budget e alle tue esigenze.</p> <p>Puoi contare su elevati livelli di copertura, a fronte di tariffe che ti premiano se sei un automobilista &ldquo;virtuoso&rdquo;, e puoi assicurare anche altri veicoli che usi nel tempo libero o per lavoro, come <strong>moto, camper, caravan o autocarri</strong>.</p>
