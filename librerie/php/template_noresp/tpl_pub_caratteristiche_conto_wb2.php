function validateAndSend() {
	thereisanerror = false;
	baseclassname = "";
	// check sulla select
	if ($('#select_tipo').val() === "Seleziona" ){
		
		$('#select_tipo')[0].parentNode.className =  "fieldblock onright error";
		thereisanerror = true;	
	} else {
		$('#select_tipo')[0].parentNode.className = "fieldblock onright" ;
		thereisanerror = false;
	}		
	//setta la classe di default a tutti i campi
	for (i=0; i<$('#sendemail')[0].getElementsByTagName('INPUT').length; i++) {
		if ($('#sendemail')[0].getElementsByTagName('INPUT')[i].parentNode.className.indexOf('onright')>0){
			baseclassname= "fieldblock onright";
		} else {
			baseclassname= "fieldblock";
		}
		if ($('#sendemail')[0].getElementsByTagName('INPUT')[i].value == ''){
			$('#sendemail')[0].getElementsByTagName('INPUT')[i].parentNode.className = baseclassname + " error";
			thereisanerror = true;
		} else {
			$('#sendemail')[0].getElementsByTagName('INPUT')[i].parentNode.className = baseclassname;
			if ($('#sendemail')[0].getElementsByTagName('INPUT')[i].id == 'contactUserMail') {
				var email = $('#sendemail')[0].getElementsByTagName('INPUT')[i].value;
				if (!checkMailFormat(email)) {
