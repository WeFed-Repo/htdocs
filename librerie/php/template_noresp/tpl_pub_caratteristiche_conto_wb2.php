					$('#sendemail')[0].getElementsByTagName('INPUT')[i].parentNode.className = baseclassname + " error";
					thereisanerror = true;
				}
			}
			//thereisanerror = false;
		}
	}
	
	
	if ( thereisanerror == true){
		return;
	}else{
		inviaFormBoxNumeriVerdi();
	}

}


 function checkMailFormat(mail){
	
	var esito=false;
	
	var espressione = /^[_a-z0-9+-]+(\.[_a-z0-9+-]+)*@[a-z0-9-]+(\.[a-z0-9-]+)+$/;	
	
		var mail2bchk= mail;
		mail2bchk=mail2bchk.replace(/^\s*/, "").replace(/\s*$/, "");
		if(espressione.test(mail2bchk))
			esito=true;
		else
			return false;

	return esito;
}

