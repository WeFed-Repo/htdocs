	<p>Il contribuente &egrave; tenuto a riportare con <strong>particolare attenzione</strong> il codice fiscale, i dati anagrafici e il domicilio fiscale.</p>
	
	<p>Nelle apposite colonne delle varie sezioni del modello devono essere evidenziati i <strong>codici tributo</strong> o le <strong>causali contributo</strong> per i quali si effettua il versamento e l'anno cui si riferisce il versamento stesso, da indicare con quattro cifre (es.: 2002).</p>
	
	<p>I soggetti il cui periodo d'imposta non coincide con l'anno solare, devono barrare l'apposita casella ed indicare nella colonna "<strong>anno di riferimento</strong>" il primo dei due anni solari interessati.</p>
	
	<p>Gli importi devono sempre essere indicati con le <strong>prime due cifre decimali</strong> anche nel caso che tali cifre siano pari a zero (es.: somma da versare pari a 52 euro, va indicato 52,00). In presenza di pi&ugrave; cifre decimali occorre procedere all'arrotondamento della seconda cifra decimale con il seguente criterio:</p>
	
	<ul>
		<li>se la terza cifra &egrave; inferiore a 5, l'arrotondamento al centesimo va effettuato per difetto (es.: euro 52,752 arrotondato diventa euro 52,75);</li>
		<li>se la terza cifra &egrave; uguale o superiore a 5, l'arrotondamento va effettuato per eccesso (es.: euro 52,755 arrotondato diventa euro 52,76, euro 52,758 arrotondato diventa euro 52,76).</li>
	</ul>


