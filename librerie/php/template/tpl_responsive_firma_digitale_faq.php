        <li>Installa sul tuo computer Adobe Reader XI (11.0) in italiano.</li>
        <li>Puoi scaricare gratuitamente la versione aggiornata al seguente indirizzo: http://get.adobe.com/it/reader.</li>
        <li>Apri il documento firmato digitalmente.</li>
        <li>Dalla barra degli strumenti seleziona Modifica > Preferenze. Si aprir&agrave; una finestra.</li>
