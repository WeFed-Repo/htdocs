<h2>Come richiedere il fido</h2>
<section>
<p>Richiedi il tuo fido con pochi e semplici passi.</p>

<p>Per attivare il fido &egrave; necessario:</p>
<ul>
	<li>chiedere alla banca l'invio della documentazione contrattuale, indicando il conto corrente d'appoggio e l'importo desiderato</li>
	<li>stampare e firmare la modulistica;</li>
	<li>allegare alla documentazione una copia delle ultime due buste paga e restituire il tutto a Webank.</li>
</ul>

<p>La banca effettuer&agrave; una valutazione della richiesta al fine di decidere se concedere il fido. 
In caso di esito positivo la banca ti comunicher&egrave; la concessione del fido ed entro pochi giorni il fido sar&agrave; attivo.</p>

<p>Ti ricordiamo che i requisiti necessari per l'attivazione del fido* sono:</p>
<ol>
	<li>essere titolari di un Conto Webank attivo da almeno 6 mesi;</li>
	<li>aver domiciliato l'accredito dello stipendio da almeno 3 mesi sul Conto Webank.</li>
</ol>

<p>Puoi richiedere subito il tuo fido, riservandoti di utilizzarlo all'occorrenza.</p>

<p class="note">
* La richiesta del fido  soggetta a valutazione della pratica da parte di Webank. Condizioni contrattuali disponibili sul F.I 
</p>

