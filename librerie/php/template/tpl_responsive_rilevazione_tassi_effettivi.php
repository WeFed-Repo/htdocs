<h2>Rilevazione tassi effettivi</h2>

<section>
<p>
	Il "<strong>Tasso Effettivo Globale Medio</strong>" o <strong>(TEGM)</strong> fornisce elementi utili ad accertare se le condizioni di costo (spese, interessi e oneri di varia natura, escluse quelle per imposte e tasse) delle operazioni creditizie praticate dalle banche e dagli intermediari finanziari presentano carattere usurario. Le operazioni creditizie sono a tal fine ripartite in categorie omogenee (per esempio: aperture di credito in conto corrente, crediti personali, mutui, ... ect.) e, all'interno delle singole categorie omogenee, suddivise per classi di importo.
</p>
<p>
	Il <strong>TEGM</strong> &egrave; rilevato trimestralmente dal Ministero dell'economia e delle finanze ed è pubblicato sulla Gazzetta Ufficiale. 
</p>
<p>
	Per verificare se un tasso di interesse è usurario, e quindi vietato, bisogna individuare, tra tutti quelli pubblicati, il tasso relativo alla categoria di operazioni in cui il credito è compreso, aumentarlo di un quarto e aggiungere un margine di ulteriori quattro punti percentuali. Il risultato di tale operazione non può essere superiore a otto punti percentuali rispetto al tasso medio di riferimento, sulla base della Legge n. 108/96 e successive modifiche introdotte dal DL. 70/2011.
</p>	
