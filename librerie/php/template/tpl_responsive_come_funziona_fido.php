<h2>Come funziona</h2>

<section>
<p>
	Il fido &egrave; l'importo che la banca mette a disposizione del cliente, in aggiunta al saldo disponibile sul conto corrente, consentendogli di utilizzarlo per gestire al meglio ogni esigenza di spesa. L'importo del fido si somma al saldo del conto corrente, aumentando il margine immediatamente disponibile.
</p>

<p>
	Potrai utilizzare il fido, per esempio, per effettuare dei pagamenti in prossimità della fine del mese anche prima dell'accredito dello stipendio o in previsione di un entrata futura già prevista.
</p>
<p>
	Grazie al fido Webank avrai quindi:
</p>

<ul>
	<li>
		<strong>pi&ugrave; flessibilit&agrave;.</strong> Potrai utilizzare con la massima elasticit&agrave; le somme messe a disposizione dalla banca per effettuare le tue spese e i tuoi pagamenti, rimborsando poi le somme utilizzate con successivi versamenti, bonifici o altri accrediti in conto corrente, ripristinando di volta in volta la disponibilit&agrave; di spesa.
	</li>
	<li>
		<strong>pi&ugrave; comodit&agrave;.</strong> Una volta attivato il fido potrai usufruirne in qualsiasi momento senza ulteriori necessit&agrave; formali. Il fido, infatti, &egrave; a tempo indeterminato.
	</li>
	<li>
		<strong>meno costi.</strong> L'attivazione del fido non comporta l'addebito di alcuna commissione. Gli interessi passivi saranno calcolati esclusivamente sull'importo affidato e limitatamente ai giorni di effettivo utilizzo, senza l'applicazione di alcuna ulteriore commissione.
	</li>
</ul>

<p class="note">
	La richiesta di attivazione del fido &egrave; soggetta a valutazione ed approvazione da parte della banca.
</p>
