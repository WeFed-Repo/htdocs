<section>
    <h3>    
       Informativa ex. art. 5 del  " Codice Deontologico sui sistemi di informazioni creditizie"
    </h3>
    <p>
    	La Banca per concederLe un finanziamento, utilizza alcuni dati che La riguardano. Si tratta di informazioni che Lei stesso ci fornisce o che otteniamo consultando alcune banche dati. Senza questi dati, che ci servono per valutare la Sua affidabilità, potrebbe non esserLe concesso il finanziamento.
    </p>      
</section>


