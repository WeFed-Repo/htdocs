<h2>Cos'&egrave; una domiciliazione</h2>

<p>La domiciliazione bancaria permette di pagare <strong>utenze</strong> e <strong>spese mensili</strong> come bollette, affitti o pagamenti collegati a strumenti finanziari.</p>

<section>
<h3>Sepa Direct Debit</h3>

<p>
	La <strong>Direttiva Europea sui Sistemi di Pagamento</strong> <a href="#" data-toggle="modal" data-target="#layerSepa">(SEPA)</a>
	ha introdotto l'<strong>addebito diretto SEPA</strong> (S.D.D - Sepa Direct Debit -). 
	E' uno <strong>strumento di incasso</strong> a livello europeo che consente di farsi addebitare in automatico il proprio conto corrente a 
	seguito di un accordo scritto (<strong>mandato</strong>) tra creditore e debitore.
</p>
</section>

<section>
<h3>Cosa Cambia</h3>

<ul>
	<li>
		Dal <strong>1&deg; maggio 2014</strong> i <strong>Rid ordinari,</strong> con i quali &egrave; possibile ad esempio domiciliare sul conto le bollette, 
		sono stati dismessi e <strong>sostituiti</strong> dal servizio di addebito europeo, <strong>Sepa Direct Debit (SDD).</strong>
	</li>
	<li>
		Dal <strong>1&deg; febbraio 2016</strong> anche i <strong>Rid finanziari e i Rid a importo fisso</strong> sono dismessi. 
		Le autorizzazioni all'addebito 
		rilasciate come Rid manterranno <strong>validit&agrave;</strong> e le aziende creditrici potranno continuare a incassare avvalendosi del 
		servizio di addebito europeo SDD Core. Alla ricezione da parte della banca della prima richiesta di addebito come SDD 
		le deleghe esistenti come Rid (finanziari o a importo fisso) <strong>saranno automaticamente trasformate</strong> in Mandati SDD Core, 
		finanziari o a importo fisso.
	</li>
	<li>
		Per queste due tipologie di SDD <strong>non &egrave; prevista la facoltà di storno</strong> entro le 8 settimane.
	</li>
</ul>
</section>

