											<p>La <strong>plusvalenza</strong> (capital gain, in inglese) &egrave; quello che si guadagna come differenza fra il prezzo di acquisto e il prezzo di vendita. In caso di perdita si parla di <strong>minusvalenza</strong> (capital loss). </p>
											<p>Se si compra un'azione a 100 e la si rivende a 110, la differenza (110-100) costituisce una plusvalenza. Quando invece il prezzo di vendita &egrave; inferiore a quello di acquisto, si ha una perdita o minusvalenza.</p>
											<p>Il decreto sopra citato prevede la possibilit&agrave; di compensare le plusvalenze tramite eventuali minusvalenze realizzate in precedenza sul mercato obbligazionario, azionario, dei derivati e dal rimborso delle sicav.</p>
											<p>Le <strong>minusvalenze</strong> in base all'attuale normativa possono venire compensate nell'anno in cui si realizzano o entro i quattro anni successivi alla loro realizzazione.</p>
											<p>Ad esempio le perdite realizzate nel 2010 potranno venire recuperate fino al 31/12/2014: dopo tale data decade la possibilità di usufruire di questo credito d'imposta.</p>
