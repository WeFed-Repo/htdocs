							Una volta inseriti tutti i dati invia la delega F24 <b>indicando la "data di pagamento" (fino a +25 giorni di calendario) in cui desideri venga effettuato l'addebito.</b> Si ricorda che la disposizione &egrave; revocabile fino al  giorno lavorativo precedente alla data pagamento indicata. Dopo aver trasmesso la disposizione telematica, la Banca effettuer&agrave; le necessarie verifiche e render&agrave; consultabile sulla tua postazione il relativo esito:<br/>
							
