<h2>Requisiti per i prestiti</h2>

<p>
	La <strong>valutazione effettiva</strong> della richiesta di prestito inoltrata online &egrave; svolta dopo la consegna della documentazione e la firma della domanda di prestito da parte dei richiedenti.
</p>
<p>
	Prima di procedere alla richiesta di prestito &egrave; utile verificare anche l'esistenza di alcuni <strong>prerequisiti necessari</strong> e prendere visione della <strong>documentazione</strong> da allegare alla domanda. 
</p>

