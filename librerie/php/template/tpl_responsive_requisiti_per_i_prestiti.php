		<h3>Residenza</h3>

		<p>
			Per richiedere un prestito &egrave; necessario che i richiedenti siano <strong>residenti in Italia.</strong>
		</p> 

		<p>
			&Egrave; considerato residente (persona fisica) il cittadino italiano con dimora abituale in Italia ed il cittadino "non italiano" con dimora abituale in Italia (quindi con regolare permesso di soggiorno) limitatamente alle attivit&agrave; lavorative svolte in Italia per un periodo non inferiore ai 183 giorni nell'anno solare.
		</p>

		<h3>Et&agrave;</h3>
		<p>
			Per richiedere un prestito Webank &egrave; necessario essere maggiorenni. 
			Il finanziamento deve concludersi prima che i richiedenti compiano il 70&deg; anno di et&agrave;. 
