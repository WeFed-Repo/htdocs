<section>
<p class="note">
	L'Isc è un <strong>valore orientativo</strong>. Non considera l'imposta di bollo obbligatoria sul conto corrente stabilita dallo Stato e gli interessi passivi maturati. Lo trovi sul <strong>Foglio informativo</strong> del conto corrente. Lo trovi anche sul <strong>Documento di sintesi di fine anno:</strong> ti permette di confrontare i costi per ogni profilo con quelli effettivi (cioè che hai sostenuto per tutti i servizi bancari che hai usato) indicati nell'estratto conto al 31 dicembre. 		
</p>
