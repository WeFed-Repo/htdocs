<h2>Indicatore sintetico di costo</h2>


<section>
<p>	
	<strong>L'Indicatore sintetico di costo (ISC)</strong> è stato introdotto da Banca d'Italia per misurare il <strong>costo annuale</strong> dei conti correnti sul mercato, confrontarli secondo <strong>criteri omogenei</strong> e scegliere il più vantaggioso. 
</p>

<p>
	<strong>Come funziona l'ISC</strong><br/>
	L'Isc è espresso in euro e considera alcune voci di spesa, fisse e variabili: canone, commissioni sulle operazioni, costo dei servizi eccetera. Non è assoluto: ogni conto corrente ha un Isc per ogni tipologia di cliente. Le tipologie sono definite da Banca d'Italia in <strong>profili di utilizzo
	</strong>. 
</p>

<p>
	<strong>Che cosa sono i profili</strong><br/>
	Chi ha un conto corrente è classificato indicativamente in un profilo secondo i suoi <strong>comportamenti bancari </strong>(quali e quante operazioni fa, con quale frequenza e attraverso quali canali, quali strumenti di pagamento usa). I primi 6 profili riguardano i conti correnti "a pacchetto" (quelli più diffusi con tariffe a forfait, come Conto Webank). Il settimo riguarda quelli a consumo. Per saperne di più leggi la <a href="#!" class="btn-icon">classificazione di Banca d'Italia.</a>
</p>

