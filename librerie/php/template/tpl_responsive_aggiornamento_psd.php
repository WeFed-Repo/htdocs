<h2>Aggiornamento PSD</h2>

<section>
<p>	
	La nuova <strong>Direttiva europea sui servizi di pagamento (Psd)</strong> contiene le norme che regolano i pagamenti nei paesi dell'Unione Europea.
	È nata per realizzare un mercato unico dei pagamenti in euro, creare più concorrenza tra le banche e garantire ai consumatori europei <strong>servizi pi&ugrave; sicuri e condizioni pi&ugrave; trasparenti</strong>. È entrata in vigore in Italia il <strong>1° marzo 2010.</strong>
</p>
<p>
	Stiamo lavorando per adeguare i nostri servizi di pagamento alle nuove regole senza modificare le tue abitudini nel fare le operazioni. Dove le regole richiedono cambiamenti, abbiamo migliorato le funzioni di pagamento sul sito per renderle <strong>ancora più semplici.</strong>
</p>
<p>
	Aggiorniamo questa pagina ogni volta che modifichiamo le funzioni o ne introduciamo di pi&ugrave; innovative secondo le novit&agrave; della Psd.
	Passa di qua, ogni tanto. Ti avvisiamo delle novit&agrave; sempre e comunque anche via email.
</p>	
</section>

