			Il tasso di interesse &egrave; il <strong>costo percentuale</strong> che il cliente corrisponde alla banca calcolato sul capitale finanziato.
			Le formule di prestito proposte su questo sito sono a tasso <strong>fisso,</strong> la rata &egrave; costante per tutta la durata del finanziamento. Il valore del tasso &egrave; fissato, inizialmente, di solito in funzione di uno specifico parametro di mercato. 
		</p>
		<p>
			<strong>Indici di riferimento</strong><br/>
			Banca Popolare di Milano utilizza gli indici di riferimento <strong>pi&ugrave; comuni</strong> per la definizione dei tassi sui prestiti.
		</p>
		<ul>
			<li>
				Tasso <strong>fisso:</strong> &egrave; calcolato in base al valore dell'IRS di periodo (Interest Rate Swap riferito alla durata del prestito), aumentato di uno "spread" percentuale, definito per ogni prodotto.
			</li>	
		</ul>
