       	<p>
       		Il rimborso del prestito &egrave; scandito dalle rate, che sono addebitate in conto corrente con periodicit&agrave;
       		<strong> mensile.</strong>
			L'insieme delle rate costituisce il <strong>piano di rimborso</strong> del capitale finanziato. Il piano di rimborso adottato per i prestiti personali Banca Popolare di Milanoprevede la formula di "ammortamento alla francese", ossia...
		</p>
		<ul>
			<li>ogni rata &egrave; caratterizzata da una quota di capitale restituito e da una quota di interessi sul prestito;</li>
			<li>per ogni rata, le quote di capitale rimborsate sono crescenti nel tempo, mentre gli interessi sono decrescenti.</li>
		</ul>
		<p>
			In questo modo per i prestiti a tasso fisso il valore della rata &egrave; costante per tutta la durata del prestito, 
			cio&egrave; &egrave; sempre dello stesso importo.
		</p>

		<p>
			<strong>Rimborso anticipato</strong><br/>
			Il prestito pu&ograve; essere rimborsato anticipatamente dal cliente, in parte o per tutto il capitale residuo da restituire a una certa data. E' sufficiente darne disposizione alla banca.
		</p>

		<ul>
			<li>
				Se il rimborso &egrave; parziale, il cliente pu&ograve; optare per la riduzione dell'importo della singola rata, ferma restando la scadenza del prestito.
			</li>
			<li>	
				Se il rimborso &egrave; totale, si ottiene l'estinzione anticipata del prestito.
			</li>	
		</ul>

		<p>
			Sui rimborsi anticipati parziali non &egrave; applicata alcuna penale.
		</p>

		<p>
			<strong>Rate in mora</strong><br/>
			Il ritardato o il mancato pagamento della rata comporta l'applicazione degli "interessi di mora".
		</p>
