<section>
<h3>Vita e multirischi (Cpi mutui privati)</h3>
<p>
	<strong>La polizza facoltativa che tutela te e la tua famiglia.</strong><br/>
	Ti offre una protezione in caso di imprevisti che compromettono la capacit&agrave; di rimborso del finanziamento. 
</p> 

<p>La polizza prevede:</p>
<ul> 
	<li>
		<strong>il rimborso del capitale residuo</strong> in caso di <strong>Decesso</strong> o di 
		<strong>Invalidità Totale Permanente;</strong></li>
	<li>
		<strong>il rimborso delle rate</strong> di mutuo nei casi di <strong>Inabilità Temporanea Totale</strong> e di 
		<strong>Ricovero Ospedaliero.</strong>
	</li>
</ul>

<p>
	La copertura può essere attivata in quote proporzionali su tutti gli intestatari del mutuo, fino al compimento del 75° anno di età e fino ad un importo massimo di 350.000 euro per ogni assicurato. 
</p>

<p>Il <strong>premio annuale è pari allo 0,39%</strong> del capitale da rimborsare:</p> 
<ul>
	<li>al momento dell'adesione per il primo anno</li>
	<li>all'inizio di ogni anno per le annualità successive, fino alla scadenza del mutuo.</li>
</ul>
<p>
	La polizza è stipulata in convenzione con <strong> Bipiemme Vita S.p.A. e Bipiemme Assicurazioni S.p.A.</strong> 
</p>
