<h2>Il Crimeware</h2>

<section>
<h3>Cosa &egrave; il crimeware</h3>

<p>
	Il crimeware &egrave; una <strong>nuova modalit&agrave; di furto dell'identit&agrave; elettronica</strong>, legata alla diffusione presso i pc dei clienti di homebanking di codice malevolo (<strong>maleware</strong>) in grado di
</p>

<ul>
	<li>reperire le informazioni personali disponibili sui PC contaminati</li>
	<li>presentare sui PC contaminati pagine web di richiesta dati personali</li>
	<li>trasmettere i dati reperiti al frodatore</li>
</ul>

<p>I pi&ugrave; diffusi tipi di maleware sono: <strong>virus, worm, trojan, spyware, dialer, keylogger.</strong></p> 

<p>Questi software arrivano sul pc in 3 modi:</p>

<ul>
	<li>attraverso un supporto (floppy, cd rom) che li contiene;</li>
	<li>attraverso la posta elettronica;</li>
	<li>scaricando dati da internet o navigando su siti non sicuri.</li>
</ul>

<h3>Come difendersi</h3>

<p>
	&Egrave; possibile migliorare costantemente la sicurezza del proprio computer e ridurre la possibilit&agrave; di infezione grazie a piccoli accorgimenti:
</p>

<ul>
	<li>Installare sul pc un <strong>software antivirus e aggiornarlo periodicamente,</strong> consentendo allo strumento di riconoscere e rimuovere le nuove minacce.</li>
	<li><strong>Non aprire</strong> allegati di posta elettronica inviati da mittenti sconosciuti.</li>
	<li>Fare regolarmente una <strong>scansione</strong> dell'intero sistema del computer con l'antivirus.</li>
</ul>

