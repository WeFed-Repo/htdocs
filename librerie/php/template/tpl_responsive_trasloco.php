	<h3>Come traslocare</h3>
	<ol>
		<li>Scarica, stampa e compila il <strong>modulo</strong> e la <strong>lettera di autorizzazione.</strong></li>
		<li>Scansionali e spediscili all'indirizzo email Traslocoinwebank@webank.it oppure recati in una filiale BPM per consegnare il modulo compilato.<br>
		Nel caso in cui l'invio sia via email ricordati di apporre la <strong>data di efficacia</strong> pari al tredicesimo giorno lavorativo dalla data di invio del modulo. Recandoti in filiale la data di efficacia sar&agrave; compilata dall'operatore di agenzia.</li>
	</ol>
