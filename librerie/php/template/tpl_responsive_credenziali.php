	<h3>Assistenza password</h3>
	<h4>Password di I livello smarrita o bloccata.</h4>
	<p>Se hai <strong>smarrito, dimenticato o bloccato</strong> la password con cui accedi normalmente al sito (il sistema blocca l'accesso alle funzioni <strong>dopo cinque inserimenti di password errata</strong>), chiamaci al numero verde <strong>800 060 070</strong>: riceverai indicazioni precise per accedere nuovamente al tuo conto. Ricorda: all'accesso dovrai scegliere una nuova password di I livello; dovrà essere diversa dalla precedente. 
	Puoi invece continuare ad utilizzare la tua Carta Servizi Telematici che contiene i 32 codici della password di II livello.</p>
	<h4>Password di II livello smarrita o bloccata</h4>
	<p>Se hai <strong>smarrito o bloccato</strong> la tua <strong>password di II livello</strong> (i 32 codici contenuti nella tua Carta Servizi Telematici) , o se hai il sospetto che qualcuno ne sia venuto a conoscenza puoi richiederne una nuova entrando in Webank con user ID e password di I livello e cliccando su "Hai perso la Carta Servizi Telematici?". </p>
</section>