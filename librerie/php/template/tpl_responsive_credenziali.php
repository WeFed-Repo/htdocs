<h2>Le tue credenziali</h2>

<section>
	<p><strong>User ID (codice cliente) </strong>: &egrave;  un codice di 7 cifre che non  puoi modificare. 
	Lo ricevi all'apertura del conto, per email o per posta.</p>
	<p><strong>Password iniziale: </strong> ti occorre per accedere al sito la prima volta e dovr&agrave; essere obbligatoriamente modificata:</p>
	<ul>
		<li>se l'hai ricevuta via SMS, all'apertura del conto, avrà validità solo fino al completamento del primo accesso;</li>
		<li>diversamente, se l'hai ricevuta per posta conservala.</li>
	</ul>
	<p>Password di I livello: &egrave; la password che scegli al primo accesso nel cambio password obbligatorio. Con questa operazione sostituisci la password iniziale con una di tua scelta composta da un minimo di 8 ad un massimo di 16 caratteri alfanumerici; </p>
	<p>Password di II livello (Carta Servizi Telematici): &egrave; un ulteriore codice personale di sicurezza costituito da 32 codici di 4 cifre. Nei casi in cui sia previsto l'utilizzo della password di II livello, il sistema ti richiede 2 delle 4 cifre che compongono uno dei 32 codici. La riceverai per posta su tessera plastificata. </p>
	<p>Numero di cellulare: &egrave; un elemento fondamentale per accedere al Programma Sicurezza, l'iniziativa di Webank con la quale potrai ricevere</p>
	<ul>
		<li>la notifica delle operazioni effettuate sul tuo conto corrente, gratuitamente e in tempo reale</li>
		<li>una One Time Password (OTP), un codice che ti invieremo per autorizzare alcune disposizioni particolarmente importanti.</li>
	</ul>
	<p>
		Potrai inoltre decidere di ricevere, a pagamento, ulteriori informazioni relative al tuo conto attivando il servizio di &quot;Alert SMS&quot; su particolari eventi.
	Per questo &egarve; importante che il numero di cellulare che certifichi (confermi) appartenga solo a te e sia costantemente aggiornato.
	Se non l'hai ancora fatto, certifica il tuo cellulare al percorso &quot;My Home > Dati e impostazioni personali > Modifica recapiti > Cellulare&quot;. 
	</p>
	<p>Indirizzo email: è un altro elemento fondamentale del Programma Sicurezza di Webank con il quale potrai ricevere:</p>
	<ul>
		<li>lla notifica delle operazioni effettuate sul tuo conto corrente, gratuitamente e in tempo reale</li>
		<li>le comunicazioni di servizio da parte della tua Banca</li>
		<li>una One Time Password (OTP), un codice che ti invieremo per autorizzare alcune disposizioni particolarmente importanti.</li>
	</ul>
	<p>Per tutte queste ragioni &egrave; importante che l'indirizzo email appartenga solo a te sia confermato.
	Se non l'hai ancora fatto, devi certificarlo al percorso &quot;My Home > Dati e impostazioni personali > Modifica recapiti > Email&quot;.</p>
</section>

