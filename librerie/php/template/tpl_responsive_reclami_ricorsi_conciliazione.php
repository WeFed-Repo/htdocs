<h3>Ricorsi e conciliazione</h3>
<p>
	Dal <strong>21 marzo 2011</strong> è obbligatoria la <strong>mediazione</strong> nei casi di una controversia, fra l'altro, in materia di contratti bancari, finanziari e assicurativi, come stabilito dal Decreto Legislativo 4 marzo 2010, 
	n. 28. <br/>
	La mediazione è l'attività professionale svolta da un <strong>terzo imparziale</strong> e finalizzata ad <strong>assistere</strong> due o più soggetti sia nella <strong>ricerca di un accordo amichevole
	</strong> per la composizione di una controversia, sia nella <strong>formulazione di una proposta
	</strong> per la risoluzione della stessa.
	Il mediatore è la persona o le persone fisiche che, individualmente o collegialmente, svolgono la mediazione rimanendo prive, in ogni caso, del potere di rendere giudizi o decisioni vincolanti per i destinatari del servizio medesimo. Il mediatore è un professionista con requisiti di terzietà. L'organismo dove il mediatore presta la sua opera è vigilato dal Ministero della giustizia. <br/>
	La mediazione può svolgersi presso <strong>enti pubblici o privati</strong>, che sono iscritti nel registro tenuto presso il Ministero della giustizia e che erogano il servizio di mediazione nel rispetto della legge, del regolamento ministeriale e del regolamento interno di cui sono dotati, approvato dal Ministero della giustizia.<br/>
	BPM aderisce all'<strong>Organismo costituito dal</strong> <a href="#">Conciliatore BancarioFinanziario</a> - <strong>Associazione per la risoluzione delle controversie bancarie, finanziarie e societarie - ADR,</strong> iscritto nel registro degli organismi tenuto presso il Ministero della Giustizia.<br/>
	In materia bancaria e finanziaria, il procedimento di mediazione può essere esperito anche presso l'
	<a href="#!">Arbitro Bancario e Finanziario (ABF)</a> costituito presso la Banca d'Italia, in materia inerente ai servizi di investimento e accessori presso l'<a href="#!">Arbitro per le Controversie Finanziarie (ACF)</a> istituito presso la Consob.
</p>
