		<h3>Rimborso anticipato</h3>

		<p>
			Il prestito pu&ograve; essere rimborsato anticipatamente dal cliente, <strong>in parte o per tutto il capitale</strong> residuo da restituire a una certa data. &Egrave; sufficiente darne disposizione alla banca:
		</p>
		<ul>
			<li>
				se il rimborso &egrave; parziale, il cliente pu&ograve; optare per la riduzione dell'importo della singola rata ferma restando la scadenza del prestito.
			</li>
			<li>
				se il rimborso &egrave; totale, si ottiene l'estinzione anticipata del prestito.
			</li>
		</ul>	
		<p>
			Sui rimborsi anticipati (parziali o totali) <strong>non &egrave; applicata alcuna penale.</strong>
		</p>	


		<h3>Rate in mora</h3> 

		<p>
			Il ritardato o il mancato pagamento della rata comporta l'applicazione degli "interessi di mora", normalmente pari a una <strong>maggiorazione del 1%</strong> sul tasso del prestito. 
		</p>
		<p>
			<strong>Attenzione:</strong> il reiterato ritardo nei pagamenti della rata pu&ograve; comportare la risoluzione del contratto di prestito da parte della banca!
		</p>

