			&Egrave; calcolato come percentuale del capitale erogato (ovvero del residuo da restituire) durante tutta la durata del prestito. 
		</p>
		<p>
			Le formule di prestito proposte sono a tasso fisso o variabile.
		</p>
		<ul>
			<li><strong>Prestiti a tasso fisso:</strong> la rata &egrave; costante per tutta la durata del finanziamento. Il valore del tasso &egrave; fissato inizialmente, normalmente in funzione di uno specifico parametro di mercato.</li>
			<li><strong>Prestiti a tasso variabile:</strong> la rata &egrave; variabile nel tempo. Il valore del tasso cambia periodicamente (potenzialmente anche per ogni rata del prestito) in funzione dell'andamento di mercato del parametro di indicizzazione definito.</li>
		</ul>	


		<p>
			<strong>Prestito Webank</strong> &egrave; proposto sempre nella formula a tasso fisso. Il valore del tasso &egrave; fissato inizialmente, come valore predefinito. Con Conto Webank la <strong>convenienza</strong> e la <strong>trasparenza</strong> sono incluse nel servizio.
