<h2>Il phishing</h2>
<section>
	<p>
	Il phishing &egrave; una frode informatica tesa a  compiere un <strong>furto d'identità digitale</strong>, rubando online i dati personali di un utente (es. chiavi di accesso al servizio di home banking, numero di carta di credito,...). Il phishing <strong>viene attuato da truffatori</strong> che inviano <strong>false email</strong> apparentemente provenienti da una banca o da una società emittente carte di credito, composte utilizzando il logo, il nome e la grafica tipici dell'azienda imitata.<br>
	Queste email invitano il destinatario a collegarsi tramite un link a un sito Internet del tutto simile a quello della banca e a inserirvi, le informazioni riservate.
	</p>
</section>
<section>
	<h3>Come difendersi</h3>
	<p>&Egrave; importante fare attenzione alla gestione della posta elettronica, leggendo con cura i messaggi che ricevi.</p>
	<p><strong>Nelle email di phishing</strong> il mittente chiede al destinatario di comunicare i propri dati personali (tipicamente i codici segreti per accedere a servizi on line) rispondendo al messaggio, oppure suggerisce di cliccare un link, inserito nella email stessa, per collegarsi ad una pagina web in cui inserire i dati richiesti.</p>
	<p><strong>Ricordati di... </strong></p>
	<ol>
   		<li><strong>Non rispondere</strong> mai a questo tipo di email.</li>
        <li>Non cliccare i link proposti</li>
        <li>Non inserire i dati richiesti nella email o nelle pagine web proposte</li>
        <li>Accedi al sito, digitando per esteso l'indirizzo www.xxxxx. it direttamente nella barra del browser.</li>
        <li>In caso di <strong>dubbio</strong> su un messaggio, rivolgiti al <strong>Numero Verde</strong> per segnalare l'accaduto.</li>
     </ol>
</section>
