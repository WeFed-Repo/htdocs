									<h2>Rapporti dormienti</h2>
									<h4>DEPOSITI DORMIENTI</h4>
									<p>
									Il 17 agosto 2007 &egrave; entrato in vigore il DPR 22.6.2007 n. 116, che regola i cosiddetti "Depositi Dormienti".
									</p>

									<div class="sep"></div>

									<h4>CHE COSA SONO</h4>
									<p>
									Tutti i depositi bancari che <b>non</b> risultino <b>movimentati, per iniziativa del cliente, da oltre 10 anni</b> sono considerati dormienti. Il DPR stabilisce che tali depositi con saldo superiore a 100 euro e non movimentati anche dopo adeguata informativa <b>vengano estinti e le relative somme girate ad un Fondo</b> istituito presso il Ministero dell'Economia e delle Finanze con la finalit&agrave; di risarcire i risparmiatori vittime di frodi finanziarie (legge 23.12.2005 n.266, art. 1, comma 345).
									</p>
									<div class="sep"></div>
									<h4>COME LA BANCA INFORMA I CLIENTI INTERESSATI</h4>
									<p>
									Per tutelare i risparmiatori, mettendoli in condizione di "riattivare" il rapporto prima che le somme vengano girate al Fondo ministeriale, la normativa prevede che le Banche provvedano ad informare la propria clientela interessata dal decreto.
									</p>


									<div class="sep"></div>
									<h4>COME SI PUO' INTERROMPERE LO STATO DI DORMIENZA DEL RAPPORTO</h4>
									<p>
									E' sufficiente che entro 180 giorni dalla data di ricezione della lettera raccomandata, i titolari dei depositi dormienti dispongano una delle seguenti operazioni:
									</p>
									<ul class="featlist">
										<li>qualsiasi operazione di <b>movimentazione del deposito</b>;</li>
										<li>consegna di un lettera attestante la <b>volont&agrave; a continuare il rapporto</b>.</li>
									</ul>
									<div class="sep"></div>
									<h4>QUALI SONO LE CONSEGUENZE PER I RAPPORTI DORMIENTI CHE NON VERRANNO RIVITALIZZATI</h4>
									<p>Trascorso il termine di 180 giorni sopra indicato, tali rapporti verranno considerati effettivamente dormienti e, unitamente agli eventuali depositi ad essi collegati, verranno estinti.
									<br /><br />
									Per richiedere il rimborso delle somme relative ai rapporti estinti il titolare del rapporto dormiente pu&ograve; inviare il seguente <a href="https://www.webank.it/doc/Modulo_dormienti_restituzione_somme_estinti.pdf" target="_blank">MODULO DI RIMBORSO</a> compilato e sottoscritto entro il <b>31 marzo 2013</b>.
									<br /><br />
									Dopo tale data le somme derivanti dall'estinzione del rapporto verranno trasferite dalla banca al fondo Istituito presso il Ministero dell'Economia e delle Finanze.
									<br /><br />
									<a href="https://www.webank.it/doc/Dormienti_2014.pdf" target="_blank">LISTA DEPOSITI DORMIENTI ESTINTI AL 31 DICEMBRE 2013 </a>
									</p>
