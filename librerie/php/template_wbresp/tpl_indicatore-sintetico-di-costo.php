                                    <h2>Indicatore sintetico di costo</h2>
                                    <p>
                                        L&rsquo;<b>Indicatore Sintetico di Costo (ISC)</b> &egrave; stato introdotto Banca d&rsquo;Italia per misurare il <b>costo annuale</b> dei conti correnti sul mercato, confrontarli secondo <b>criteri omogenei</b> e scegliere il pi&ugrave; vantaggioso.
                                        <br /><br />
                                    </p>

                                    <h4>COME FUNZIONA L'ISC</h4>
                                    <p>
                                        L&rsquo;Isc &egrave; espresso in euro e considera alcune voci di spesa, fisse e variabili: canone, commissioni sulle operazioni, costo dei servizi eccetera. Non &egrave; assoluto: ogni conto corrente ha un Isc per ogni tipologia di cliente. Le tipologie sono definite da Banca d&rsquo;Italia in <b>profili di utilizzo</b>.
                                        <br /><br />
                                    </p>

                                    <h4>CHE COSA SONO I PROFILI</h4>
                                    <p>
                                        Chi ha un conto corrente &egrave; classificato indicativamente in un profilo secondo i suoi <b>comportamenti bancari</b> (quali e quante operazioni fa, con quale frequenza e attraverso quali canali, quali strumenti di pagamento usa). I primi 6 profili riguardano i conti correnti &ldquo;a pacchetto&rdquo; (quelli pi&ugrave; diffusi con tariffe a forfait, come Conto Webank). Il settimo riguarda quelli a consumo. Per saperne di pi&ugrave; leggi la <a href="https://www.webank.it/doc/ProfiliBankitalia.pdf" target="_blank">classificazione di Banca d'Italia</a>.
                                        <br /><br />
                                        Qui sotto trovi la tabella che descrive indicativamente i profili e mostra il valore dell'Isc di Conto Webank.
