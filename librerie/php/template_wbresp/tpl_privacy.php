                                            <h5>COOKIES<span class="espandi">Espandi</span> <span class="comprimi">Comprimi</span></h5>
                                            <div class="punto-content">
                                                <br />
                                                <b>DEFINIZIONE DEI COOKIES</b>
                                                <br /><br />

                                                I cookie sono stringhe di testo di piccole dimensioni che i siti visitati dall'utente inviano al suo terminale (solitamente al browser), dove vengono memorizzati per essere poi ritrasmessi agli stessi siti alla successiva visita del medesimo utente.
                                                <br /><br />
                                                <b>UTILIZZO DEI COOKIES</b>
                                                <br /><br />
                                                Quando accedi alle pagine dei siti del Gruppo Banca Popolare di Milano vengono inviati sul device che utilizzi (computer, telefono, tablet, ecc.)  dei cookies &laquo; tecnici &raquo;, &laquo;di profilazione &raquo;,  anche di &laquo; parti terze &raquo;
                                                <br /><br />
                                                <b>I COOKIES TECNICI</b>
                                                <br /><br />
                                                Sono cookies fondamentali per navigare all&rsquo;interno del sito ed utilizzare tutte le funzionalit&agrave;, come ad esempio l&rsquo;accesso alle aree riservate. Senza questi cookie non sarebbe possibile fornire i servizi richiesti. Questi cookies non raccolgono informazioni da utilizzare per fini commerciali.
                                                <br /><br />
                                                I cookie tecnici possono essere:
                                                <br /><br />
                                                Di sessione: garantiscono la normale navigazione e fruizione del sito web (permettendo, ad esempio, di realizzare un acquisto o autenticarsi per accedere ad aree riservate).
                                                <br /><br />
                                                Funzionali: permettono all'utente la navigazione in funzione di una serie di criteri selezionati (ad esempio, la lingua, i prodotti selezionati per l'acquisto) al fine di migliorare il servizio reso allo stesso.
                                                <br /><br />
                                                Analytics: raccolgono informazioni, in forma aggregata, sul numero degli utenti e su come questi visitano il sito stesso.
                                                <br /><br />
                                                <b>I COOKIES DI PROFILAZIONE</b>
                                                <br /><br />
                                                Permettono di tracciare un profilo degli utenti raccogliendo informazioni sulle loro preferenze di navigazione, per poi inviare contenuti personalizzati (banner promozionali e/o offerte dedicate). I cookies di profilazione possono essere anche inviati da societ&agrave; terze.
