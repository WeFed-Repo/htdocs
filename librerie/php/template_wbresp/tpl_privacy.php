                                            <b>Direttiva 95/46/CE</b> - del 24 ottobre 1995 relativa alla &ldquo;tutela delle persone fisiche con riguardo al trattamento dei dati personali, nonch&eacute; alla libera circolazione di tali dati&rdquo;
                                            <br /><br />
                                            <b>Direttiva 2002/58/CE</b> - relativa al &ldquo;trattamento dei dati personali e alla tutela della vita privata nel settore delle comunicazioni elettroniche&rdquo;
                                            <br /><br />
