                                        <div class="punto">
                                            <h5>DIRITTI DEGLI INTERESSATI<span class="espandi">Espandi</span> <span class="comprimi">Comprimi</span></h5>
                                            <div class="punto-content">
                                                <br />
                                                <b>DIRITTI DEGLI INTERESSATI</b>
                                                <br /><br />

                                                I soggetti cui si riferiscono i dati personali hanno il diritto in qualunque momento di ottenere la conferma dell&rsquo;esistenza o meno dei medesimi dati e di conoscerne il contenuto e l&rsquo;origine, verificarne l&rsquo;esattezza o chiederne l&rsquo;integrazione o l&rsquo;aggiornamento, oppure la rettificazione (art. 7 del Decreto legislativo 30 giugno 2003, n. 196). Ai sensi del medesimo articolo si ha il diritto di chiedere la cancellazione, la trasformazione in forma anonima o il blocco dei dati trattati in violazione di legge, nonch&eacute; di opporsi in ogni caso, per motivi legittimi, al loro trattamento.
                                                <br /><br />
                                                <b>DOMANDE, RECLAMI E SUGGERIMENTI</b>
                                                <br /><br />
                                                Chiunque fosse interessato a maggiori informazioni, a contribuire con propri suggerimenti o avanzare reclami o contestazioni in merito alle politiche privacy della societ&agrave; o sul modo in cui i Titolari del trattamento trattano i dati personali, pu&ograve; effettuarlo scrivendo al seguente indirizzo e-mail <a href="mailto:protezionedati@bpm.it">protezionedati@bpm.it</a>
