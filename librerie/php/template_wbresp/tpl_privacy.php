                                        <div class="punto">
                                            <h5>PRINCIPI GENERALI<span class="espandi">Espandi</span> <span class="comprimi">Comprimi</span></h5>
                                            <div class="punto-content">
                                                <br />
                                                <b>IL &quot;TITOLARE&quot; DEL TRATTAMENTO</b>
                                                <br /><br />
                                                L&rsquo;accesso e l&rsquo;utilizzo dei servizi on line in essere prevede il trattamento di dati relativi a persone identificabili mediante sistemi e procedure di autenticazione.<br />
                                                La mera consultazione del sito internet, senza accesso ai servizi online, prevede la raccolta di informazioni la cui trasmissione &egrave; implicita nell&rsquo;uso dei protocolli di comunicazione di Internet e di informazioni relative all&rsquo;elaboratore utilizzato per la connessione.<br />
                                                &quot;Titolari&quot; autonomi del trattamento sono le Banche del Gruppo: Banca Popolare di Milano e Banca Popolare di Mantova, la prima per tutti i trattamenti che attengono l&rsquo;erogazione dei servizi di home banking del Gruppo BPM.
                                                <br />

                                                <br />
                                                <b>LUOGO DI TRATTAMENTO DEI DATI</b>
                                                <br /><br />
                                                I trattamenti connessi ai servizi web di questo sito hanno luogo presso le sedi dei Titolari e sono curati solo da personale incaricato del trattamento, oppure da eventuali incaricati di occasionali operazioni di manutenzione. I dati personali degli utenti che accedono e utilizzano i servizi online potranno essere comunicati a terzi soggetti rispetto al Gruppo BPM, ci&ograve; in adempimento ad obblighi di legge, obblighi regolamentari ed obblighi di vigilanza.
                                                <br />

                                                <br />
                                                <b>MODIFICHE ALLE PRESENTI POLITICHE SULLA PRIVACY</b>
                                                <br /><br />
                                                I Titolari del trattamento verificano periodicamente le proprie politiche relative alla privacy e alla sicurezza e, se del caso, le rivede in relazione alle modifiche normative, organizzative o dettate dall'evoluzione tecnologica. In caso di modifica delle politiche, la nuova versione sar&agrave; pubblicata in questa pagina del sito.
                                                <br /><br />
                                            </div>
                                        </div>

                                        <div class="punto">
                                            <h5>TIPI DI DATI TRATTATI<span class="espandi">Espandi</span> <span class="comprimi">Comprimi</span></h5>
                                            <div class="punto-content">
                                                <br />
                                                <b>DATI DI NAVIGAZIONE</b>
                                                <br /><br />

                                                Consultazione, accesso ed  utilizzo dei servizi on-line
                                                <br /><br />
                                                I sistemi informatici e le procedure software preposte al funzionamento di questo sito web acquisiscono, nel corso del loro normale esercizio, alcuni dati personali la cui trasmissione &egrave; implicita nell&rsquo;uso dei protocolli di comunicazione di Internet.
                                                <br /><br />
                                                Si tratta di informazioni che non sono raccolte per essere associate a interessati identificati, ma che per loro stessa natura potrebbero, attraverso elaborazioni ed associazioni con dati detenuti da terzi, permettere di identificare gli utenti.
                                                <br /><br />
                                                In questa categoria di dati rientrano gli indirizzi IP o i nomi a dominio dei computer utilizzati dagli utenti che si connettono al sito, gli indirizzi in notazione URI (Uniform Resource Identifier) delle risorse richieste, l&rsquo;orario della richiesta, il metodo utilizzato nel sottoporre la richiesta al server, la dimensione del file ottenuto in risposta, il codice numerico indicante lo stato della risposta data dal server (buon fine, errore, ecc.) ed altri parametri relativi al sistema operativo e all&rsquo;ambiente informatico dell&rsquo;utente.
                                                <br /><br />
                                                Questi dati vengono utilizzati ai soli fini di:
                                                <ul class="featlist">
                                                    <li>ricavare informazioni statistiche anonime sull&rsquo;uso del sito;</li>
                                                    <li>controllare il corretto funzionamento del sito;</li>
                                                    <li>svolgere attivit&agrave; di monitoraggio a supporto della sicurezza del servizio.</li>
                                                </ul>

                                                I dati potrebbero essere conservati ed utilizzati per l&rsquo;accertamento di responsabilit&agrave; in caso di ipotetici reati informatici ai danni del sito o dei suoi utilizzatori.
                                                <br /><br />
                                                <b>DATI FORNITI VOLONTARIAMENTE DALL&rsquo;UTENTE </b><br />
                                                Accesso ed utilizzo dei servizi online
                                                <br /><br />
                                                L&rsquo;inserimento libero, esplicito e volontario di dati personali da parte dell&rsquo;utente nei form di registrazione presenti su questo sito comporta la successiva acquisizione dei dati forniti dal mittente, necessari per l&rsquo;erogazione del servizio richiesto. Specifiche informative di sintesi verranno a seconda dei casi riportate o visualizzate nei documenti contrattuali o nelle pagine del sito predisposte per particolari servizi a richiesta.
                                                <br /><br />
                                                <b>COOKIES</b>
                                                <br /><br />
                                                Il sito utilizza <b>cookies di sessione</b> e <b>cookies statici</b> per la tracciatura della navigazione degli utenti; i dati vengono utilizzati unicamente in forma anonima e statistica per finalit&agrave; di natura statistica interna nonch&eacute;, in associazione con i dati dell&rsquo;utente, per le attivit&agrave; di monitoraggio antifrode in adempimento agli obblighi di legge.
                                                <br /><br />
                                            </div>
                                        </div>



                                        <div class="punto">
                                            <h5>MODALIT&Agrave; DEL TRATTAMENTO<span class="espandi">Espandi</span> <span class="comprimi">Comprimi</span></h5>
                                            <div class="punto-content">
                                                <br />
                                                <b>FACOLTATIVIT&Agrave; DEL CONFERIMENTO DEI DATI</b>
                                                <br /><br />

                                                A parte quanto specificato per i dati di navigazione, l&rsquo;utente &egrave; libero di fornire i dati personali riportati nei moduli di richiesta o comunque indicati in contatti con l'Ufficio per sollecitare l&rsquo;invio di materiale informativo o di altre comunicazioni. Il loro mancato conferimento potrebbe comportare l&rsquo;impossibilit&agrave; di ottenere quanto richiesto.
                                                <br /><br />
                                                <b>MODALIT&Agrave; DEL TRATTAMENTO</b>
                                                <br /><br />
                                                I dati personali sono trattati con strumenti automatizzati per il tempo strettamente necessario a conseguire gli scopi per cui sono stati raccolti.
                                                <br /><br />
                                                Specifiche misure di sicurezza sono osservate per prevenire la perdita dei dati, usi illeciti o non corretti ed accessi non autorizzati.
                                                <br /><br />
                                                <b>CONDIVISIONE, COMUNICAZIONE E DIFFUSIONE DEI DATI</b>
                                                <br /><br />
                                                I dati raccolti potranno essere trasferiti o comunicati ad altre societ&agrave; controllanti, controllate o collegate nell&rsquo;ambito del Gruppo BPM per attivit&agrave; strettamente connesse e strumentali all&rsquo;operativit&agrave; del servizio.
                                                <br /><br />
                                                I dati personali forniti dagli utenti sono utilizzati al fine di eseguire il servizio o la prestazione richiesta nonch&eacute; per informazioni di natura pubblicitaria o promozionale e sono comunicati a terzi nel solo caso in cui ci&ograve; sia a tal fine necessario per la fornitura dei servizi richiesti.
                                                <br /><br />
                                                Al di fuori di questi casi, i dati non saranno comunicati, n&eacute; concessi ad alcuno, salvo previsione contrattuale o autorizzazione dei soggetti. In questo senso, i dati personali potrebbero essere trasmessi a terze parti, ma solo ed esclusivamente nel caso in cui:<br />
                                                1. vi sia consenso esplicito a condividere i dati con terze parti;<br />
                                                2. vi sia la necessit&agrave; di condividere con terzi le informazioni al fine di prestare il servizio richiesto; <br />
                                                3. ci&ograve; sia necessario per adempiere a richieste dell'Autorit&agrave; Giudiziaria o di Pubblica Sicurezza.
                                                <br /><br />
                                                Nessun dato derivante dal servizio web viene diffuso.

                                                <br /><br />
                                            </div>
