			<h2>PIANI INDIVIDUALI DI RISPARMIO (PIR)
				<span>Per beneficiare delle agevolazioni fiscali investendo nella crescita del Paese</span>
</h2>

			<p>
				I <strong>Piani Individuali di Risparmio (PIR)</strong>, introdotti dalla Legge di Bilancio 2017, hanno l'obiettivo di far confluire parte del risparmio degli italiani verso le piccole e medie imprese <strong>sostenendo la crescita economica del Paese</strong>.
			</p>
			<p>
				Investendo in un <strong>Fondo PIR</strong> fino ad un massimo di <strong>30.000 euro all'anno</strong> (150.000 euro in totale) e mantenendo il tuo investimento per <strong>almeno 5 anni</strong> potrai beneficiare di due <strong>importanti incentivi fiscali:</strong>
			</p>
			<ul>
				<li><strong>100% di esenzione</strong> dalla <strong>tassazione sugli utili;</strong></li>
				<li><strong>100% di esenzione</strong> delle <strong>imposte di successione.</strong></li>
			</ul>
			<p>&nbsp;</p>
		</div>
	</div>
