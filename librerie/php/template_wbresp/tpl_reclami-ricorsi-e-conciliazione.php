										<h4>Ricorsi e conciliazione</h4>
										<p>
										Dal <b>21 marzo 2011</b> &egrave; obbligatoria la <b>mediazione</b> nei casi di una controversia, fra l'altro, in materia di contratti bancari, finanziari e assicurativi, come stabilito dal Decreto Legislativo 4 marzo 2010, n. 28.
										<br /><br />
										La mediazione &egrave; l'attivit&agrave; professionale svolta da un <b>terzo imparziale</b> e finalizzata ad <b>assistere</b> due o pi&ugrave; soggetti sia nella <b>ricerca di un accordo amichevole</b> per la composizione di una controversia, sia nella <b>formulazione di una proposta</b> per la risoluzione della stessa.
										<br /><br />
										Il mediatore &egrave; la persona o le persone fisiche che, individualmente o collegialmente, svolgono la mediazione rimanendo prive, in ogni caso, del potere di rendere giudizi o decisioni vincolanti per i destinatari del servizio medesimo. Il mediatore &egrave; un professionista con requisiti di terziet&agrave;. L'organismo dove il mediatore presta la sua opera &egrave; vigilato dal Ministero della giustizia.
										<br /><br />
										La mediazione pu&ograve; svolgersi presso <b>enti pubblici o privati</b>, che sono iscritti nel registro tenuto presso il Ministero della giustizia e che erogano il servizio di mediazione nel rispetto della legge, del regolamento ministeriale e del regolamento interno di cui sono dotati, approvato dal Ministero della giustizia.
										<br /><br />
										Webank aderisce all'<b>Organismo costituito dal <a href="http://www.conciliatorebancario.it/" target="_blank">Conciliatore BancarioFinanziario</a></b> – <b>Associazione per la risoluzione delle controversie bancarie, finanziarie e societarie</b> – <b>ADR</b>, iscritto nel registro degli organismi tenuto presso il Ministero della Giustizia.
										<br /><br />
										In materia bancaria e finanziaria, il procedimento di mediazione pu&ograve; essere esperito anche presso la <a href="http://www.camera-consob.it/" target="_blank"><b>Camera di conciliazione della Consob</b></a> o presso l'<a href="http://www.arbitrobancariofinanziario.it/" target=""><b>Arbitro bancario e finanziario</b></a> costituito presso la Banca d'Italia.
