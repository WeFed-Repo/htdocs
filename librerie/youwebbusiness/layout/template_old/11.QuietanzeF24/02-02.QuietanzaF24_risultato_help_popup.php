						<p>Per ogni esito sono presenti i seguenti dati:</p>
						
						<ul>
							<li><span class="textblack">Contribuente</span>: soggetto che ha eseguito il versamento;</li> 
							<li><span class="textblack">Spedizione</span>: data di invio del pagamento F24. Cliccando 
							sul link &egrave; possibile visualizzare il dettaglio della delega inviata;</li>
							<li><span class="textblack">Pagamento</span>: data in cui la banca ha eseguito il pagamento della delega. 
							Cliccando sul link &egrave; possibile visualizzare il dettaglio del pagamento;</li>
							<li><span class="textblack">Tipo esito</span>: tipologia di esito (Deleghe F24 o F24 Immatricolazioni);</li>
							<li><span class="textblack">Saldo</span>: saldo finale della delega F24, risultante dalla sommatoria 
							dei saldi delle sezioni d&#39;imposta compilate;</li>
							<li><span class="textblack">Div.</span>: divisa in cui &egrave; espresso il saldo della delega;</li>
							<li><span class="textblack">ABI</span>: codice ABI della Banca ricevente (Banca di addebito);</li>
							<li><span class="textblack">CAB</span>: codice CAB dello sportello della Banca di addebito; </li>
							<li><span class="textblack">Conto</span>: conto corrente di addebito;</li>
							<li><span class="textblack">Prot.</span>: numero di protocollo assegnato alla delega;</li>
							<li><span class="textblack">Estratto</span>: data relativa all&#39;ultima estrazione dell&#39;esito di quietanza.</li>
						</ul>	
							
