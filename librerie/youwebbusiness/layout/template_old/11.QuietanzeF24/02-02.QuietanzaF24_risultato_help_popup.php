						<span class="textblack">N.B.</span> Si visualizzano solo gli esiti in stato Pagato.</p>
						
						<p>In entrata si visualizzano gli esiti secondo i parametri di ricerca impostati nella maschera 
						precedente <span class="textblack">Filtro Download Quietanze F24</span>, 
						oppure si interroga la lista completa nel caso di nessuna selezione.</p> 

						<p>L&#39; utente pu&ograve; indicare il numero di esiti visualizzabili per pagina scegliendo fra 
						<span class="textblack">10</span>, <span class="textblack">20</span>, <span class="textblack">50</span>.
						&Egrave; possibile impostare un ordinamento crescente o decrescente per data <span class="textblack">Spedizione</span>, 
						data <span class="textblack">Pagamento</span>, <span class="textblack">Tipo Esito</span>, 
						<span class="textblack">Saldo</span> e <span class="textblack">ABI</span>, 
