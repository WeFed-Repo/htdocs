    /**
     * Accede alla pagina di analisi relativa alla funzione selezionata nella
     * functionMenu (oggetto "select" di tipo AF, AQ o AT).
     * Il titolo di riferimento viene identificato da alcuni parametri "hidden"
     * nella pagina interna attuale (titoliCheck, codiceStrumento..).
     */
    function submitFunctionMenu(functionMenu) {

        var params = document.getElementById(functionMenu).value.split("|");
        var twebEvent = params[0];
        var nomeMetodo = null;
        var oggetto = null;
        var filtro = 'DEFAULT';

        // Controllo se devo impostare altri parametri nascosti per la tipologia di analisi
        // AF: NomeMetodo=<valore>, es. NomeMetodo=Introduzione
        // AQ, AT: Object=<object>, Filter=<filter>, es. Object=IndicatoriForza, Filter=DEFAULT
        for (i=1; i<params.length; i++){
            var param = params[i].split("=");
            if (param[0] == 'NomeMetodo'){
                nomeMetodo = param[1];
            } else if (param[0] == 'Object'){
                oggetto = param[1];
            } else if (param[0] == 'Filter'){
                filtro = param[1];
            }
        }
        goToFunction(twebEvent,nomeMetodo,oggetto,filtro);
    }

