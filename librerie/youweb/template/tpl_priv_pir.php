			<div class="section clearfix">
				<h3 class="titleSection">Cosa sono o piani individuali di risparmio?</h3>
				<p>I Piani Individuali di Risparmio, PIR, sono una nuova forma di investimento che permette di ottenere forti incentivi fiscali introdotta dalla Legge 11 dicembre 2016, n. 232  (Legge di Bilancio 2017). Sono riservati alle singole persone fisiche residenti fiscalmente in Italia e sono a tutti gli effetti dei&laquo;contenitori&raquo; nei quali possono essere inseriti diversi tipi di strumenti finanziari, nel rispetto di quanto previsto dalla normativa.<br>
				I Piani individuali di Risparmio possono configurarsi come: <strong><u>fondi comuni d'investimento</u></strong>, contratti assicurativi, gestioni patrimoniali o dossier titoli.<br>
				<strong>I Fondi PIR, detti anche PIR-compliant sono «investimenti qualificati» e conformi alla disciplina PIR  come  definita dalla &laquo;Legge di Bilancio 2017&raquo;.</strong>
				</p>
			</div>
			<div class="section clearfix">
				<h3 class="titleSection">Quali sono i vantaggi fiscali per chi investe in un PIR?</h3>
				<p>Un PIR consente di ottenere 2 vantaggi fondamentali:</br>
				- <strong>100% di esenzione dalla tassazione</strong> su utili, interessi, cedole e dividendi generati dall'investimento (normalmente le plusvalenze sono tassate tra il 26% e il 12,5% come nel caso dei titoli di stato);<br>
				-<strong>100% di esenzione</strong> delle <strong>imposte di successione.</strong>
					
				
			</div>
			<div class="section clearfix">
				<h3 class="titleSection">Come ottenere questi vantaggi?</h3>
				<p>Per ottenere queste agevolazioni fiscali occorre che l’investimento in PIR rispetti determinati vincoli:<br>
				-<strong>composizione del portafoglio</strong> > mantenere almeno il 70%  investito in azioni e obbligazioni di emittenti italiani o di emittenti europei stabili in Italia (ad esclusione del settore immobiliare),  almeno il 30% del precedente 70% (il 21%) deve essere investito in società a media/piccola capitalizzazione;<br>
				-<strong>ammontare investito</strong> > l'investimento massimo annuale è di 30.000 € , per un totale complessivo di 150.000 € in 5 anni;<br>
				-<strong>orizzonte temporale dell'investimento</strong> > un PIR deve essere mantenuto in portafoglio per almeno 5 anni.
			</p>
				
			</div>
