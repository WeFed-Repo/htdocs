			<div class="section clearfix">
				<h3 class="titleSection">L'unione fa la forza</h3>
				<p>Un fondo comune d'investimento (&laquo;fondo&raquo;) &egrave; paragonabile a una <strong>cassa comune</strong> in cui una <strong>pluralit&agrave; di risparmiatori decide di versare i propri risparmi</strong> con l'obiettivo di ottenere nel tempo una crescita del valore dei risparmi versati.</p>
			</div>
			<div class="section clearfix">
				<h3 class="titleSection">Come funzionano?</h3>
				<p>I fondi comuni d'investimento sono gestiti da <strong>Case specializzate nella gestione professionale del risparmio collettivo (SGR o SICAV)</strong>. Queste Case ricevono delega dai risparmiatori per gestire collettivamente e unitariamente il patrimonio del fondo e fanno tutte le necessarie scelte d'investimento. Il patrimonio del fondo &egrave; suddiviso in «quote» per le SGR o &laquo;azioni&raquo; per le SICAV assegnate ai risparmiatori proporzionalmente a quanto hanno versato nel fondo. Il valore della &laquo;quota o azione&raquo; &egrave; unitario (N.A.V./Valore Attivo Netto del fondo) e rappresenta il valore di tutti i titoli all'interno del fondo. Il valore varia in funzione dell'andamento dei mercati finanziari e dei titoli e viene aggiornato quotidianamente o settimanalmente a seconda della tipologia di Fondo.</p>
			</div>
			<div class="section clearfix">
				<h3 class="titleSection">Quali sono le principali tipologie di fondi?</h3>
				<p><strong>I fondi</strong> appartengono alla categoria degli <strong>Oicr</strong> (Organismi di investimento collettivo del risparmio) e si distinguono in:<br>
				
				-<strong>fondi comuni</strong> di diritto italiano istituiti gestiti da <strong>SGR</strong> (Societ&agrave; di Gestione di Risparmio): se investi in un fondo ricevi &laquo;quote di partecipazione&raquo; del fondo;<br>
				-<strong>fondi comuni</strong> di diritto estero sono istituiti e gestiti da <strong>SICAV</strong> (Societ&agrave; d'Investimento a Capitale Variabile): se investi in comparti di una SICAV ricevi &laquo;azioni&raquo; della SICAV.
				</p>
