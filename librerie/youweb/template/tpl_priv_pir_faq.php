				<div id="accordion" class="accordion nosort">
					<h3><a class="news_title" href="#">1) Chi pu&ograve; investire in un Piano Individuale di Risparmio (PIR)?</a></h3>
					 <div class="ui-accordion-content ui-helper-reset ui-widget-content ui-corner-bottom ui-accordion-content-active" role="tabpanel" style="display: block;">
						 <p>I Piani Individuali di Risparmio (PIR) possono essere sottoscritti esclusivamente da tutte le persone fisiche residenti ai fini fiscali in Italia e sono sottoposti ad un Regime fiscale amministrato.</p>
					 </div>
					<h3><a class="news_title" href="#">2) Per investire in un Piano Individuale di Risparmio devo prima aprire un deposito PIR?</a></h3>
					 <div class="ui-accordion-content ui-helper-reset ui-widget-content ui-corner-bottom ui-accordion-content-active" role="tabpanel">
						 <p>YouWeb consente di usufruire delle agevolazioni fiscali introdotte dalla &laquo;Legge di Bilancio 2017&raquo; per i Piani Individuali di Risparmio mediante la sottoscrizione di Fondi PIR compliant, investimenti di per sé qualificati  e conformi alla disciplina PIR.<br>
						Con la prima sottoscrizione in un Fondo PIR deleghi la Casa di Gestione ad aprire una rubrica PIR abbinata al tuo codice fiscale e a fare da sostituto d'imposta. 
						</p>
					 </div>
					 <h3><a class="news_title" href="#">3) Quali sono i vincoli da rispettare per usufruire delle agevolazioni fiscali dei PIR?</a></h3>
					 <div class="ui-accordion-content ui-helper-reset ui-widget-content ui-corner-bottom ui-accordion-content-active" role="tabpanel">
						 <p>Investendo in un Fondo PIR compliant dovrai:<br>
						- Investire al massimo 30.000 &euro; all'anno, fino ad un  massimo complessivo &egrave; di 150.000 &euro;;
						- Mantenere l'investimento per almeno 5 anni (potrai disinvestire le somme accantonate il 1 anno solare a partire dal 6 anno, e cos&igrave; via)<br>
						Non dovrai preoccuparti di altro in quanto la Casa di Gestione &egrave; responsabile della composizione di portafoglio secondo normativa e del suo mantenimento durante il periodo di investimento.
						</p>
					 </div>
					 <h3><a class="news_title" href="#">4) Quale regime fiscale viene applicato ai Piani Individuali di Risparmio (PIR)?</a></h3>
					 <div class="ui-accordion-content ui-helper-reset ui-widget-content ui-corner-bottom ui-accordion-content-active" role="tabpanel">
						 <p>Come previsto dalla legge 11 dicembre 2016, n. 232 (Legge di Bilancio 2017) la Casa di gestione all'apertura della rubrica PIR in fase di prima sottoscrizione  provvede all'applicazione del <strong>regime fiscale amministrato</strong> per il tuo investimento in PIR.
						</p>
					 </div>
					  <h3><a class="news_title" href="#">5) Quanto costa un PIR?</a></h3>
					 <div class="ui-accordion-content ui-helper-reset ui-widget-content ui-corner-bottom ui-accordion-content-active" role="tabpanel">
						 <p>I Fondi PIR prevedono gli stessi costi dei Fondi NON PIR, sia in termini di Commissioni di Sottoscrizione/Switch/Uscita che di Costi di Gestione. Per i dettagli consulta il dettaglio del Fondo e la Documentazione Legale d’Offerta.
						</p>
					 </div>
					  <h3><a class="news_title" href="#">6) Posso sottoscrivere un PIC e PAC su un fondo PIR?</a></h3>
					 <div class="ui-accordion-content ui-helper-reset ui-widget-content ui-corner-bottom ui-accordion-content-active" role="tabpanel">
						 <p>Si, &egrave; consentito investire in un fondo PIR sia in modalit&agrave; PIC che PAC purch&egrave; sia rispettato il limite massimo annuale di 30.000&euro; fino ad un totale di 150.000&euro; in 5 anni. Per il PAC, ad esempio, la rata mensile potr&agrave; essere al massimo di 2.500&euro; e la rata trimestrale al massimo 7.500&euro;, nessun vincolo per la durata del tuo PAC.
						</p>
					 </div>
					  <h3><a class="news_title" href="#">7) Quanto posso investire in modalit&agrave; PAC  su un Fondi PIR?
</a></h3>
					 <div class="ui-accordion-content ui-helper-reset ui-widget-content ui-corner-bottom ui-accordion-content-active" role="tabpanel">
						 <p>Fermo restando i massimali d'investimento previsti dalla normativa PIR, &egrave; possibile attivare un PAC a partire da 50 &euro; al mese fino ad un massimo di 2.500&euro;.
						</p>
					 </div>
					 <h3><a class="news_title" href="#">8) Posso sospendere un PAC su un fondo PIR?</a></h3>
					 <div class="ui-accordion-content ui-helper-reset ui-widget-content ui-corner-bottom ui-accordion-content-active" role="tabpanel">
						 <p>Puoi sospendere i versamenti del tuo PAC in un fondo PIR in qualsiasi momento e le quote investite se mantenute in portafoglio per almeno 5 anni non perderanno i benefici fiscali.
						</p>
					 </div>
					 <h3><a class="news_title" href="#">9) Posso essere titolare di pi&ugrave; di un Piano Individuale di Risparmio?
?</a></h3>
					 <div class="ui-accordion-content ui-helper-reset ui-widget-content ui-corner-bottom ui-accordion-content-active" role="tabpanel">
						 <p>Non &egrave; possibile essere titolare di pi&ugrave;
						  di un PIR contemporaneamente. Pu&ograve; esistere un solo rapporto PIR in capo ad un Codice fiscale e non sono ammesse le cointestazioni. Al momento della sottoscrizione l'investitore rilascia un'autocertificazione di non possedere altrui PIR presso altri collocatori.


						</p>
					 </div>
					  <h3><a class="news_title" href="#">10) Posso sottoscrivere pi&ugrave; di un fondo PIR della stessa Casa di Gestione?

?</a></h3>
					 <div class="ui-accordion-content ui-helper-reset ui-widget-content ui-corner-bottom ui-accordion-content-active" role="tabpanel">
						 <p>Si, &egrave; consentito investire in differenti fondi PIR purch&egrave; della stessa Casa di Gestione e nel rispetto del limite massimo annuale di 30.000 &euro; e totale di 150.000 &euro;. I Fondi PIR sottoscritti confluiranno nella medesima rubrica PIR che viene aperta dalla Casa al momento della prima sottoscrizione.
						</p>
					 </div>
					  <h3><a class="news_title" href="#">11) Posso sottoscrivere pi&ugrave; di un fondo PIR di diverse Case di Gestione?


?</a></h3>
					 <div class="ui-accordion-content ui-helper-reset ui-widget-content ui-corner-bottom ui-accordion-content-active" role="tabpanel">
						 <p>Pu&ograve; esistere un unico rapporto PIR in capo al singolo Codice fiscale. La rubrica PIR abbinata al tuo codice fiscale viene creata dalla Casa di Gestione prescelta pertanto non è possibile sottoscrivere fondi PIR di diverse Case di Gestione ed essere titolare di pi&ugrave; rubriche PIR.
						</p>		
					 </div>
					  <h3><a class="news_title" href="#">12) &Egrave; possibile effettuare switch tra diversi fondi PIR compliant mantenendo i benefici fiscali?



?</a></h3>
					 <div class="ui-accordion-content ui-helper-reset ui-widget-content ui-corner-bottom ui-accordion-content-active" role="tabpanel">
						 <p>Si, &egrave; possibile effettuare il passaggio da un fondo PIR ad un altro fondo PIR purch&egrave; appartenenti alla stessa Famiglia (o linea di fondi) senza fare decadere i benefici fiscali in quanto i Fondi PIR sono inseriti in un unico rapporto PIR presso la Casa di Gestione. Ciò nel rispetto dei massimali investibili previsti dalla normativa.

						</p>		
					 </div>
					 <h3><a class="news_title" href="#">13) &Egrave; possibile effettuare switch da un Fondo NON PIR ad un Fondo PIR?</a></h3>
					 <div class="ui-accordion-content ui-helper-reset ui-widget-content ui-corner-bottom ui-accordion-content-active" role="tabpanel">
						 <p>Prima di switchare da un Fondo NON PIR ad un Fondo PIR dovrai effettuare una prima sottoscrizione del Fondo PIR (per l’apertura della rubrica PIR presso la Casa di Gestione). Solo successivamente, a rubrica PIR aperta, potrai effettuare lo switch, sempre nel rispetto dei massimali investibili previsti dalla normativa.


						</p>		
					 </div>
					 <h3><a class="news_title" href="#">14) &Egrave; necessario attendere 5 anni prima che si ottengano benefici fiscali?
</a></h3>
					 <div class="ui-accordion-content ui-helper-reset ui-widget-content ui-corner-bottom ui-accordion-content-active" role="tabpanel">
						 <p>Per i Fondi a distribuzione di proventi, da subito non sono dovute le imposte sui dividendi incassati nell'ambito di un PIR. Tuttavia l’esenzione dal pagamento delle imposte sulle plusvalenze maturate &egrave; subordinato alla detenzione del piano individuale di risparmio per un periodo di 5 anni. 

						</p>		
					 </div>
					  <h3><a class="news_title" href="#">15) Sono obbligato a tenere il fondo PIR per 5 anni?

</a></h3>
					 <div class="ui-accordion-content ui-helper-reset ui-widget-content ui-corner-bottom ui-accordion-content-active" role="tabpanel">
						 <p>Il tuo investimento in Fondi PIR pu&ograve; essere rimborsato in qualsiasi momento, tuttavia l'uscita prima dei 5 anni comporta la perdita degli eventuali benefici fiscali. Rimborsando prima dei 5 anni libererai disponibilit&agrave; nel piano e potrai ricostruire il tuo PIR fino al raggiungimento dei massimali previsti dalla legge (30.000&euro; all'anno e 150.000 &euro; in totale). Esempio: se hai sottoscritto 30.000 € il 01/04/2017 e successivamente ne disinvesti 10.000 &euro;, entro il 31/12/2017 potrai eseguire altre sottoscrizioni per un massimo di 10.000 &euro;.
						</p>		
					 </div>
					  <h3><a class="news_title" href="#">16) Cosa succede se rimborso parte della mia posizione prima dei 5 anni?


</a></h3>
					 <div class="ui-accordion-content ui-helper-reset ui-widget-content ui-corner-bottom ui-accordion-content-active" role="tabpanel">
						 <p>Dovrai pagare le imposte sull'eventuale plusvalenza, nonch&eacute; le imposte non versate sui dividendi eventualmente percepiti più gli interessi legali nel caso di Fondi PIR a distribuzione di proventi. Al tempo stesso libererai disponibilità nel piano e potrai conferire nuove risorse fino al raggiungimento dei massimali previsti dalla legge (30.000€ all’anno e 150.000 &egarve; in totale). 
						</p>		
					 </div>
					  <h3><a class="news_title" href="#">17) Il cambio collocatore per un Fondo PIR mi garantisce la continuità dei benefici fiscali?



</a></h3>
					 <div class="ui-accordion-content ui-helper-reset ui-widget-content ui-corner-bottom ui-accordion-content-active" role="tabpanel">
						 <p>Il caso di cambio collocatore dovr&agrave; essere valutato caso per caso in quanto la continuità dei benefici fiscali &egrave; subordinata alla continuità del sostituto d’imposta che detiene la rubrica PIR.

						</p>		
