function setFormParameters(idForm) {
	var objForm=document.getElementById(idForm);
	if (objForm!=null) {
		for(var i=1;i<arguments.length;i+=2){
			var obj=objForm[arguments[i]];
			if (obj!=null) {				
				obj.value=arguments[i+1];
			}
		}
	}
}

