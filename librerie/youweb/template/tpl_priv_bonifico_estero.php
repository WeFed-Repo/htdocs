            var resultString = importoInt.val() + "." + importoDec.val();
            var resultFloat = parseFloat(resultString) * parseFloat(valute.find(":selected").data('tasso'));
            $("#bonificoSepaPulsanteContenitore").addClass("hide");
            $("#bonificoEsteroContenitore").removeClass("hide");
            $('#tasso').text(parseFloat(resultFloat).toFixed(2));
            tassoDiCambio.val(valute.find(":selected").data('tasso'));
            $('#valuteDivisa').val(valute.find(":selected").attr("value"));
            importoEsteroInt.val(resultFloat.toString().split('.')[0]);
            importoEsteroDec.val(resultFloat.toString().split('.')[1]);
            $("#avanti").prop("disabled", false);
            $("#spazioBianco").addClass("hide");
        }
    }
