			<h3 class="popover-title">Rischio</h3>
			<div class="popover-content">La rischiosit&agrave; di uno strumento &egrave; basata sull’Indicatore sintetico di rischio (SRRI) presente nel KIID e suddiviso in 6 classi qualitative, dove l’indicatore 1 e 2 rappresentano il rischio 1 “Minimo” fino ad arrivare al rischio 6 “Alto”.</div>
