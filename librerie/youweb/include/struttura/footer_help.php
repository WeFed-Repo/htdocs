			<div class="popover-content">Il rating calcolato da Deus Technology &egrave; un indicatore quantitativo di scoring, su scala da 1 a 5, che si basa sulla combinazione di vari elementi (lo Sharpe, la Volatilit&agrave;, il Max Drow Down - perdita massima maturata rispetto al valore di picco registrato - e il Mean Return - il ritorno atteso dall’investimento-) atti a comparare rendimento e rischio del fondo su vari orizzonti temporali. Lo strumento potrebbe essere <span style="text-decoration: underline;">privo di rating</span> in caso manchino i dati sufficienti per il calcolo, esempio: fondi che hanno meno di 3 anni di serie storica.</div>
