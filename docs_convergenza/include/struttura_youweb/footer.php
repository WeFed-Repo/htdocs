				<div data-auto="false" id="help_sharpe" class="bottom">
<h3 class="popover-title">Sharpe</h3>
<div class="popover-content">L'indice di Sharpe misura l'efficienza della gestione ed è pari al rapporto tra l'extra-rendimento del Fondo/Comparto rispetto ad un'attività non rischiosa e la sua volatilità. Evidenzia quindi quale rendimento è stato prodotto, in eccesso al rendimento di un'attività priva di rischio, per unità di rischio derivante dalla detenzione del Fondo/Comparto. Orizzonte temporale tutta la storia del fondo, granularità giornaliera.</div>
</div>
			
				<div data-auto="false" id="help_alpha" class="bottom">
<h3 class="popover-title">Alpha di Jensen</h3>
<div class="popover-content">L'alpha di Jensen è il rendimento incrementale di un fondo rispetto alla performance attesa. Un alpha positivo indica che il fondo ha una performance migliore rispetto a quanto atteso in relazione al suo beta, mentre un alpha negativo significa che il fondo ha una performance peggiore rispetto a quanto atteso in relazione al suo beta. L'alpha di Jensen è interpretata quindi come una misura di selectivity, cioè della capacità del Gestore nella selezione dei titoli più "performanti". Orizzonte temporale 1Y, granularità settimanale.</div>
</div>
			
				<div data-auto="false" id="help_beta" class="bottom">
<h3 class="popover-title">Beta</h3>
<div class="popover-content">L'indice Beta misura la sensibilità di un fondo alle oscillazioni del mercato. Per definizione il beta del mercato è pari a 1,00. Un coefficiente beta di 1,1 indica che il fondo può ottenere una performance del 10% migliore/peggiore rispetto all'indice di mercato in caso di rialzo/ribasso. Orizzonte temporale 1Y, granularità settimanale</div>
</div>
			
