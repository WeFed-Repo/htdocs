<h2>Piani di accumulo</h2>
<section>
	<p>
		Il Piano di Accumulo (Pac)  &egrave; una <strong>formula alternativa  e flessibile d'investimento</strong> in Fondi e Sicav che consente  di:</p>
		<ul>
			<li><strong>investire periodicamente piccole  somme</strong> e accumulare gradualmente un <strong>capitale importante;</strong></li>
			<li><strong>trasformare le oscillazioni dei mercati</strong> in <strong>opportunit&agrave;</strong> d'investimento frazionando gli importi nel tempo.</li>
		</ul>
	<p>Il <strong>Pac</strong> si rivolge anche a chi, pur non avendo grandi cifre a disposizione, intende investire un capitale per un obiettivo futuro, personalizzando l'investimento sulla base delle <strong>proprie esigenze</strong>. </p>
</section>
