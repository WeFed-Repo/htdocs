<section>
	<h3>I vantaggi del PAC</h3>
	<ul>
		<li><strong>Su misura:</strong> costruisci il tuo investimento in modo personalizzato e diversificato </li>
		<li><strong>Efficiente:</strong> gestisci con rate periodiche il tuo investimento in modo razionale anche nei momenti di incertezza dei mercati finanziari</li>
		<li><strong>Automatico:</strong> investi con metodo  grazie all'addebito  programmato della rata  sul tuo conto corrente</li>
		<li><strong>Flessibile:</strong> decidi in qualsiasi momento di revocare il tuo Pac, sospenderlo  o riattivarlo  senza spese o penalit&agrave;</li>
	</ul>
