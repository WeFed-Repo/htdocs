	<div class="row">
		<div class="col-sm-12">
			<h3>Cosa sono i fondi comuni?</h4>
			<h4 class="semi-bold">L'unione fa la forza</h4>
			<p>Un fondo comune d'investimento (&laquo;fondo&raquo;) &egrave; paragonabile a una <strong>cassa comune</strong> in cui una <strong>pluralit&agrave; di risparmiatori decide di versare i propri risparmi</strong> con l'obiettivo di ottenere nel tempo una crescita del valore dei risparmi versati.</strong></p>
		</div>
	</div>

	<div class="row">
		<div class="col-xs-12">
			<h4 class="semi-bold">Come funzionano?</h4>
			<p class="marginBottomNone">
				I fondi sono gestiti da <strong>Case (SGR o SICAV) specializzate nella gestione professionale del risparmio collettivo</strong>. Queste Case ricevono delega dai risparmiatori per gestire collettivamente e unitariamente il patrimonio del fondo e fanno tutte le necessarie scelte d'investimento. Il patrimonio del fondo &egrave; suddiviso in «quote» per le SGR o &laquo;azioni&raquo; per le SICAV assegnate ai risparmiatori proporzionalmente a quanto hanno versato nel fondo. Il valore della &laquo;quota o azione&raquo; &egrave; unitario (N.A.V./Valore Attivo Netto del fondo) e rappresenta il valore di tutti i titoli all'interno del fondo. Il valore varia in funzione dell'andamento dei mercati finanziari e dei titoli.
			</p>
		</div>

	</div>
</section>
