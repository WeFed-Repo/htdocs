			<ul>
				<li><strong>fondi comuni</strong> di diritto italiano istituiti gestiti da <strong>SGR</strong> (Societ&agrave; di Gestione di Risparmio): se investi in un fondo ricevi &laquo;quote di partecipazione&raquo; del fondo</li>
				<li><strong>fondi comuni</strong> di diritto estero sono istituiti e gestiti da <strong>SICAV</strong> (Societ&agrave; d'Investimento a Capitale Variabile): se investi in comparti di una SICAV ricevi &laquo;azioni&raquo; della SICAV</li>
			</ul>
			<p>Le <strong>5 principali categorie di Fondi e SICAV</strong> sono:</p>
