<script>
$(function () {
	var oldVal = 1;
	function validateSpinner (val) {
		var versamentoInput = $('#versamentoInput'),
			max = versamentoInput.spinner( "option", "max" );
		if (val === oldVal && val === max) {
			setHasErrors([{
				field: versamentoInput,
				text: "Il valore massimo è " + max
			}]);
		} else {
			resetHasErrors($('#formPac'));
		}
	    oldVal = val;
	}
	/* GESTIONE SPINNER
	    options.idInput = elemento da spinnare
	    options.step =  quanto deve aumentare e diminuire
	    options.min =  limite minimo
	    options.max =  limite massimo
	    options.culture = paese di riferimento,
	    options.numberFormat = formato numerico,
	    options.spin = funzione che parte allo spin
	 */
	setSpinner({
	    idInput: '#versamentoInput',
	    step: 1,
	    min: 1,
	    max: 2,
	    spin: function(event, ui) {
	        validateSpinner(ui.value);
	    }
	});

	/* Pulsanti */
	var paccStep1 = $('#paccStep1'),
		paccStep2 = $('#paccStep2'),
		paccStep3 = $('#paccStep3');
	$('#pacBtn1').click(function () {
		paccStep1.hide();
		paccStep2.show();
	});
	$('#pacBtn2').click(function () {
		paccStep1.show();
		paccStep2.hide();
	});
	$('#btnInfoInv').click(function () {
		paccStep2.hide();
		paccStep3.show();
	});
});
</script>
