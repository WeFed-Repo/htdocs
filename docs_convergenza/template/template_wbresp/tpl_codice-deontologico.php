                                        <h2>Codice deontologico</h2>
                                        <p>
                                            <b>INFORMATIVA EX ART. 5 DEL &ldquo;CODICE DEONTOLOGICO SUI SISTEMI DI INFORMAZIONI CREDITIZIE&rdquo;</b><br />
                                            <br />
                                            La Banca per concederLe un finanziamento, utilizza alcuni dati che La riguardano. Si tratta di informazioni che Lei stesso ci fornisce o che otteniamo consultando alcune banche dati. Senza questi dati, che ci servono per valutare la Sua affidabilit&agrave;, potrebbe non esserLe concesso il finanziamento.
                                            <br /><br />
                                        </p>


                                    </div>
