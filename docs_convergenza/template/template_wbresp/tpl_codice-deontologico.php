                                        <div class="punto">
                                            <h5>COMUNICAZIONE DEI DATI<span class="espandi">Espandi</span> <span class="comprimi">Comprimi</span></h5>
                                            <div class="punto-content">
                                                <br />
                                                Al fine di meglio valutare il rischio creditizio alcune di queste informazioni (dati anagrafici, anche della persona eventualmente coobbligata, tipologia del contratto, importo del credito, modalit&agrave; di rimborso) saranno comunicate a grandi banche dati istituite per valutare il rischio creditizio, gestite da privati e consultabili da molti soggetti. Ci&ograve; significa che altre banche o finanziarie a cui Lei chieder&agrave; un altro prestito, un finanziamento, una carta di credito, ecc., anche per acquistare a rate un bene di consumo, potranno sapere se Lei ha presentato a noi una recente richiesta di finanziamento, se ha in corso altri prestiti o finanziamenti e se paga regolarmente le rate. <br />
                                                Queste grandi banche dati prendono il nome di sistemi di informazioni creditizie e sono  regolati dal relativo codice deontologico del 2004 (Gazzetta Ufficiale del 23 Dicembre 2004, n. 300; sito web www.garanteprivacy.it). <br />
                                                Nell'ambito dei sistemi di informazioni creditizie, i Suoi dati saranno trattati secondo modalit&agrave; di organizzazione, raffronto ed elaborazione strettamente indispensabili per perseguire le finalit&agrave; sopra descritte, e in particolare per estrarre in maniera univoca dal sistema di informazioni creditizie le informazioni a Lei ascritte. Tali elaborazioni saranno effettuate attraverso strumenti informatici, telematici e manuali che garantiscono la sicurezza e la riservatezza degli stessi, anche nel caso di utilizzo di tecniche di comunicazione a distanza.<br />
                                                <br /><br />
                                                I sistemi di informazioni creditizie cui aderiamo sono gestiti da:<br />
                                                <ul class="featlist">
                                                    <li>CRIF S.p.A., con sede legale in Bologna, Ufficio Relazioni con il Pubblico: Via Montebello 2/2, 40121 Bologna. Fax: 051 6458940, Tel: 051 6458900.</li>
                                                </ul>
                                                <br /><br />
                                                Il sistema &egrave; di tipo positivo e negativo, utilizza sistemi automatici di credit scoring e vi partecipano i seguenti soggetti:  Banche, Intermediari Finanziari, soggetti privati che nell'esercizio di un'attivit&agrave; commerciale o professionale concedono dilazioni di pagamento del corrispettivo per la fornitura di beni o servizi.<br />
                                                CRIF S.p.A. aderisce ad un circuito internazionale di sistemi di informazioni creditizie operanti in vari paesi europei ed extra-europei e, pertanto, i dati trattati potranno essere comunicati (sussistendo tutti i presupposti di legge) ad altre societ&agrave;, anche estere, che operano - nel rispetto della legislazione del loro paese - come autonomi gestori dei suddetti sistemi di informazioni creditizie e quindi perseguono le medesime finalit&agrave; di trattamento del sistema gestito da CRIF S.p.A. (elenco sistemi esteri convenzionati disponibili al sito www.crif.com).<br />
