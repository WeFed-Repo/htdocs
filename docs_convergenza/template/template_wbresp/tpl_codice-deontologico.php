                                        <div class="punto">
                                            <h5>MODALITA&rsquo; DEL TRATTAMENTO<span class="espandi">Espandi</span> <span class="comprimi">Comprimi</span></h5>
                                            <div class="punto-content">
                                                <br />
                                                I Suoi dati possono essere oggetto di particolari elaborazioni statistiche al fine di attribuirLe un giudizio sintetico o un punteggio sul Suo grado di affidabilit&agrave; e solvibilit&agrave; (cd. credit scoring), tenendo conto delle seguenti principali tipologie di fattori: numero e caratteristiche dei rapporti di credito in essere, andamento e storia dei pagamenti dei rapporti in essere o estinti, eventuale presenza e caratteristiche delle nuove richieste di credito, storia dei rapporti di credito estinti. Alcune informazioni aggiuntive possono esserLe fornite in caso di mancato accoglimento di una richiesta di credito.
                                                <br /><br />
                                                Conserviamo i Suoi dati presso la nostra societ&agrave; per tutto ci&ograve; che &egrave; necessario per gestire il finanziamento e adempiere ad obblighi di legge.
                                                <br /><br />
                                                I dati che La riguardano sono aggiornati periodicamente con informazioni acquisite nel corso del rapporto (andamento dei pagamenti, esposizione debitoria residuale, stato del rapporto).
